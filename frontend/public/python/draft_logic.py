def handle_request(request: Dict[str, Any]) -> Dict[str, Any]:
    req = _plain(request) or {}
    action = req.get("action") or "initialize_draft"
    league_data = req.get("leagueData") or req.get("league") or {}
    payload = req.get("payload") or {}

    if action == "initialize_draft":
        return initialize_draft(league_data, payload)

    if action == "make_user_pick":
        return make_pick(league_data, payload, force_cpu = False)

    if action == "sim_one_pick":
        return make_pick(league_data, payload, force_cpu = True)

    if action == "sim_to_user_pick":
        return sim_until_user_pick(league_data, payload)

    if action == "sim_rest_of_draft":
        return sim_rest_of_draft(league_data, payload)

    return {
        "ok": False,
        "reason": f"UNKNOWN_DRAFT_ACTION: {action}",
        "version": DRAFT_LOGIC_VERSION,
    }
