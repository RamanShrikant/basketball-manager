def _get_season_year(league: Dict[str, Any], payload: Dict[str, Any]) -> int:
    for value in [
        payload.get("seasonYear"),
        league.get("seasonYear"),
        league.get("currentSeasonYear"),
        league.get("seasonStartYear"),
    ]:
        year = _safe_int(value, 0)
        if 2020 <= year <= 2100:
            return year
    return 2026


def _draft_class_year_from_prospect(prospect: Dict[str, Any]) -> int:
    if not isinstance(prospect, dict):
        return 0
    meta = prospect.get("meta") if isinstance(prospect.get("meta"), dict) else {}
    for value in [
        prospect.get("draftClassYear"),
        prospect.get("seasonYear"),
        prospect.get("draftYear"),
        prospect.get("rookieYear"),
        meta.get("draftYear"),
    ]:
        year = _safe_int(value, 0)
        if 2020 <= year <= 2100:
            return year
    return 0


def _draft_class_matches_season(draft_class: Any, season_year: int) -> bool:
    if not isinstance(draft_class, list) or not draft_class:
        return False

    checked = 0
    for prospect in draft_class[:10]:
        year = _draft_class_year_from_prospect(prospect)
        if year:
            checked += 1
            if year != season_year:
                return False

    return True if checked else True


def _latest_history_entry(league: Dict[str, Any], season_year: int) -> Optional[Dict[str, Any]]:
    history = league.get("seasonHistory") or []
    if not isinstance(history, list):
        return None

    matches = [
        row
        for row in history
        if isinstance(row, dict) and _safe_int(row.get("seasonYear"), 0) == season_year
    ]
    if matches:
        complete = [row for row in matches if row.get("status") == "complete"]
        return complete[-1] if complete else matches[-1]

    valid = [row for row in history if isinstance(row, dict) and isinstance(row.get("teams"), list)]
    if not valid:
        return None

    return sorted(valid, key = lambda row: _safe_int(row.get("seasonYear"), 0))[-1]


def _history_by_team(league: Dict[str, Any], season_year: int) -> Dict[str, Dict[str, Any]]:
    latest = _latest_history_entry(league, season_year)
    out: Dict[str, Dict[str, Any]] = {}
    for row in latest.get("teams", []) if latest else []:
        name = row.get("teamName") or row.get("team") or row.get("name")
        if name:
            out[name] = row
    return out


def _player_overall(player: Dict[str, Any]) -> int:
    return _safe_int(player.get("overall", player.get("ovr", player.get("rating", 70))), 70)


def _team_direction(team: Dict[str, Any], history_row: Optional[Dict[str, Any]]) -> str:
    players = team.get("players") or []
    top = sorted([_player_overall(p) for p in players if isinstance(p, dict)], reverse = True)[:8]
    avg_top = sum(top) / max(1, len(top)) if top else 72

    wins = _safe_int(history_row.get("wins"), 0) if history_row else 0
    made_playoffs = bool(history_row.get("madePlayoffs")) if history_row else False
    result = str(history_row.get("playoffResult") or "") if history_row else ""

    if result in {"champion", "finals", "conference_finals"} or wins >= 50 or (made_playoffs and avg_top >= 80):
        return "contending"
    if wins <= 34 or avg_top < 75:
        return "rebuilding"
    return "middle"


def _pos_matches(player: Dict[str, Any], pos: str) -> bool:
    primary = str(player.get("pos") or player.get("position") or "").upper()
    secondary = str(player.get("secondaryPos") or player.get("secondaryPosition") or "").upper()
    acceptable = POSITION_BUCKETS.get(pos, {pos})
    return primary in acceptable or secondary in acceptable


def _need_score(team: Dict[str, Any], pos: str) -> float:
    players = [p for p in (team.get("players") or []) if isinstance(p, dict)]
    matching = [p for p in players if _pos_matches(p, pos)]
    matching_ovrs = sorted([_player_overall(p) for p in matching], reverse = True)

    if not matching_ovrs:
        return 18.0

    best = matching_ovrs[0]
    second = matching_ovrs[1] if len(matching_ovrs) > 1 else 58
    depth_count = len([ovr for ovr in matching_ovrs if ovr >= 72])

    need = 0.0
    if best < 78:
        need += 9.0
    if best < 74:
        need += 6.0
    if second < 72:
        need += 4.0
    if depth_count <= 1:
        need += 3.0

    return max(0.0, min(18.0, need))


def _prospect_score(
    prospect: Dict[str, Any],
    team: Dict[str, Any],
    history_row: Optional[Dict[str, Any]],
    pick: Dict[str, Any],
    rng: random.Random,
) -> float:
    overall = _resolve_prospect_overall(prospect, 60)
    potential = _safe_int(prospect.get("potential"), overall)
    age = _safe_int(prospect.get("age"), 20)
    pos = prospect.get("pos") or "SF"
    pick_num = _safe_int(pick.get("pick"), 60)
    round_num = _safe_int(pick.get("round"), 1)
    direction = _team_direction(team, history_row)

    if pick_num <= 5:
        ovr_w, pot_w, need_w, ready_w = 0.56, 0.70, 0.10, 0.05
    elif pick_num <= 14:
        ovr_w, pot_w, need_w, ready_w = 0.54, 0.50, 0.20, 0.07
    elif round_num == 1:
        ovr_w, pot_w, need_w, ready_w = 0.54, 0.34, 0.36, 0.12
    else:
        ovr_w, pot_w, need_w, ready_w = 0.38, 0.44, 0.28, 0.10

    if direction == "rebuilding":
        pot_w += 0.16
        need_w -= 0.04
    elif direction == "contending":
        ovr_w += 0.10
        need_w += 0.08
        ready_w += 0.10
        pot_w -= 0.06

    need = _need_score(team, pos)
    upside_gap = max(0, potential - overall)
    nba_ready = _safe_float((prospect.get("traits") or {}).get("nbaReady"), max(0.0, (overall - 55) / 35))
    boom_bust = _safe_float((prospect.get("traits") or {}).get("boomBust"), 0.35)
    work_ethic = _safe_float((prospect.get("traits") or {}).get("workEthic"), 0.65)
    injury_risk = _safe_float((prospect.get("traits") or {}).get("injuryRisk"), 0.12)

    specialist_bonus = 0.0
    attrs = prospect.get("attrs") or []
    if len(attrs) >= 15:
        if round_num == 2:
            if attrs[0] >= 82:
                specialist_bonus += 2.2
            if attrs[8] >= 82 or attrs[10] >= 82 or attrs[12] >= 84:
                specialist_bonus += 2.0
            if attrs[13] >= 82 or attrs[14] >= 82:
                specialist_bonus += 1.2

    age_bonus = 0.0
    if direction == "rebuilding" and age <= 20:
        age_bonus += 1.5
    if direction == "contending" and age >= 21:
        age_bonus += 1.1

    risk_penalty = 0.0
    if direction == "contending":
        risk_penalty += boom_bust * 2.2
    else:
        risk_penalty += boom_bust * 0.9
    risk_penalty += injury_risk * 3.0

    randomness = rng.uniform(-2.8, 2.8)
    if round_num == 2:
        randomness = rng.uniform(-4.5, 4.5)

    score = (
        overall * ovr_w
        + potential * pot_w
        + need * need_w
        + upside_gap * 0.20
        + nba_ready * 6.0 * ready_w
        + work_ethic * 2.8
        + specialist_bonus
        + age_bonus
        - risk_penalty
        + randomness
    )

    return float(score)


def _choose_cpu_prospect(
    state: Dict[str, Any],
    league: Dict[str, Any],
    pick: Dict[str, Any],
    season_year: int,
) -> Dict[str, Any]:
    team_name = pick.get("currentOwnerTeamName") or pick.get("teamName")
    team = _find_team(league, team_name) or {"name": team_name, "players": []}
    history = _history_by_team(league, season_year).get(team_name)
    available = state.get("availableProspects") or []

    pick_num = _safe_int(pick.get("pick"), 60)
    candidate_pool_size = 18 if pick_num <= 10 else 26 if pick_num <= 30 else 34
    pool = sorted(
        available,
        key = lambda p: (_safe_int(p.get("draftProjection"), 999), _safe_int(p.get("trueRank"), 999)),
    )[:candidate_pool_size]

    rng = random.Random(_stable_seed(season_year, pick_num, team_name, "cpu_pick"))
    scored = []
    for prospect in pool:
        scored.append((_prospect_score(prospect, team, history, pick, rng), prospect))

    scored.sort(key = lambda item: item[0], reverse = True)
    return scored[0][1] if scored else available[0]


def _rookie_salary_for_pick(round_num: int, pick_num: int) -> int:
    if round_num == 1:
        # Rough rookie scale approximation. It is intentionally simple for v1.
        return max(2_400_000, int(11_800_000 - (pick_num - 1) * 315_000))

    pick_in_round = max(1, pick_num - 30)
    return max(1_250_000, int(2_250_000 - (pick_in_round - 1) * 28_000))


def _rookie_contract(round_num: int, pick_num: int, season_year: int) -> Dict[str, Any]:
    first_salary = _rookie_salary_for_pick(round_num, pick_num)

    if round_num == 1:
        salary_by_year = [
            first_salary,
            int(first_salary * 1.05),
            int(first_salary * 1.10),
            int(first_salary * 1.22),
        ]
        return {
            "type": "standard",
            "startYear": season_year,
            "salaryByYear": salary_by_year,
            "option": {
                "type": "team",
                "yearIndices": [2, 3],
                "picked": None,
            },
            "source": "first_round_rookie_scale",
            "countsAgainstStandardRoster": True,
        }

    # Second-round rookies do not become automatic standard contracts.
    # They move to the Rookie Signings event where the team can choose
    # standard contract, two-way contract, draft rights, or release.
    projected_salary_by_year = [first_salary, int(first_salary * 1.08)]
    return {
        "type": "unsigned_rookie",
        "startYear": season_year,
        "salaryByYear": [],
        "projectedSalaryByYear": projected_salary_by_year,
        "option": None,
        "pendingRookieSigning": True,
        "source": "second_round_pending_rookie_signing",
        "countsAgainstStandardRoster": False,
    }


