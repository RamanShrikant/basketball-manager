def _normalize_attrs(attrs: Any, default: int = 75) -> List[int]:
    out: List[int] = []
    if isinstance(attrs, list):
        for value in attrs[:15]:
            out.append(_clamp(_safe_float(value, default), 25, 99))
    while len(out) < 15:
        out.append(default)
    return out


def _calc_overall_from_attrs(attrs: List[int], pos: str) -> int:
    p = OVERALL_POS_PARAMS.get(str(pos or "SF").upper(), OVERALL_POS_PARAMS["SF"])
    safe_attrs = _normalize_attrs(attrs)
    weighted = sum(w * safe_attrs[i] for i, w in enumerate(p["weights"]))
    primary_indices = [max(0, int(i) - 1) for i in p["prim"]]
    peak = max(safe_attrs[i] for i in primary_indices)
    blended = p["alpha"] * peak + (1 - p["alpha"]) * weighted
    overall = round(min(99, max(60, 60 + 39 * (1 / (1 + math.exp(-0.12 * (blended - 77)))))))
    num_90 = len([a for a in safe_attrs if a >= 90])
    if num_90 >= 3:
        overall = min(99, overall + (num_90 - 2))
    return int(overall)


def _resolve_prospect_overall(prospect: Dict[str, Any], default: int = 65) -> int:
    for key in ["overall", "ovr", "rating"]:
        if prospect.get(key) is not None:
            return _safe_int(prospect.get(key), default)
    return _calc_overall_from_attrs(_normalize_attrs(prospect.get("attrs") or prospect.get("attributes") or []), prospect.get("pos") or prospect.get("position") or "SF")


def _calc_stamina_from_attrs(age: Any, attrs: List[int]) -> int:
    resolved_age = max(18, min(45, _safe_int(age, 19)))
    athleticism = max(25, min(99, _safe_int(attrs[7] if len(attrs) > 7 else 75, 75)))
    if resolved_age <= 27:
        age_factor = 1.0
    elif resolved_age <= 34:
        age_factor = 0.95 - (0.15 * (resolved_age - 28)) / 6
    else:
        age_factor = 0.8 - (0.45 * (resolved_age - 35)) / 10
    age_factor = max(0.35, min(1.0, age_factor))
    raw = age_factor * 99 * 0.575 + athleticism * 0.425
    norm = (raw - 40) / (99 - 40)
    return _clamp(40 + norm * 59, 40, 99)


def _get_all_teams(league: Dict[str, Any]) -> List[Dict[str, Any]]:
    if isinstance(league.get("teams"), list):
        return league.get("teams") or []

    conferences = league.get("conferences") or {}
    teams: List[Dict[str, Any]] = []
    if isinstance(conferences, dict):
        for conf_name, conf_teams in conferences.items():
            for team in conf_teams or []:
                if isinstance(team, dict):
                    team.setdefault("conference", conf_name)
                    teams.append(team)
    return teams


def _find_team(league: Dict[str, Any], team_name: str) -> Optional[Dict[str, Any]]:
    if not team_name:
        return None

    if isinstance(league.get("teams"), list):
        for team in league.get("teams") or []:
            if team.get("name") == team_name or team.get("teamName") == team_name:
                return team

    conferences = league.get("conferences") or {}
    if isinstance(conferences, dict):
        for teams in conferences.values():
            for team in teams or []:
                if team.get("name") == team_name or team.get("teamName") == team_name:
                    return team

    return None


