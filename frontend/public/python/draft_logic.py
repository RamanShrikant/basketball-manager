def _bankers_round_js(n: float) -> int:
    f = math.floor(float(n))
    diff = float(n) - f
    if abs(diff - 0.5) < 1e-9:
        return int(f if f % 2 == 0 else f + 1)
    return int(math.floor(float(n) + 0.5))


def _v19_jitter(name: str = "", attrs: Optional[List[int]] = None) -> float:
    safe_attrs = attrs or []
    s_attrs = 0.0
    for i, value in enumerate(safe_attrs):
        s_attrs += (i + 1) * _safe_float(value, 0.0)

    s_name = 0.0
    for ch in str(name or ""):
        s_name += ord(ch)

    seed = (s_attrs + 0.13 * s_name) * 12.9898
    raw = math.sin(seed) * 43758.5453
    frac = raw - math.floor(raw)
    return (frac - 0.5) * 0.7


def _sample_std(values: List[float]) -> float:
    n = len(values)
    if n < 2:
        return 1.0
    mean = sum(values) / n
    variance = sum((value - mean) * (value - mean) for value in values) / (n - 1)
    return max(1.0, math.sqrt(variance))


def _safe_std(value: Any) -> float:
    value = _safe_float(value, 1.0)
    return value if value > 1e-6 else 1.0


def _v19_pf_bridged_weights() -> Dict[int, float]:
    pf = OFF_WEIGHTS_POSZ["PF"]
    sf = OFF_WEIGHTS_POSZ["SF"]
    keys = set(pf.keys()) | set(sf.keys())
    return {key: 0.7 * pf.get(key, 0.0) + 0.3 * sf.get(key, 0.0) for key in keys}


def _league_players_for_v19_baselines(league: Dict[str, Any]) -> List[Dict[str, Any]]:
    conferences = league.get("conferences") or {}
    teams: List[Dict[str, Any]] = []

    if isinstance(conferences, dict):
        if "East" in conferences or "West" in conferences:
            for side in ["East", "West"]:
                teams.extend([team for team in conferences.get(side, []) if isinstance(team, dict)])
        else:
            for conf_teams in conferences.values():
                teams.extend([team for team in conf_teams or [] if isinstance(team, dict)])
    elif isinstance(league.get("teams"), list):
        teams = [team for team in league.get("teams") or [] if isinstance(team, dict)]

    players: List[Dict[str, Any]] = []
    for team in teams:
        for player in team.get("players") or []:
            if isinstance(player, dict):
                players.append(player)
    return players


def _empty_v19_baselines() -> Dict[str, Any]:
    pos_mean = {pos: {idx: 75.0 for idx in set(V19_OFF_IDX + V19_DEF_IDX)} for pos in V19_POSITIONS}
    pos_std = {pos: {idx: 1.0 for idx in set(V19_OFF_IDX + V19_DEF_IDX)} for pos in V19_POSITIONS}
    abs_mean = {idx: 75.0 for idx in V19_OFF_IDX}
    abs_std = {idx: 1.0 for idx in V19_OFF_IDX}
    return {
        "posMean": pos_mean,
        "posStd": pos_std,
        "absMean": abs_mean,
        "absStd": abs_std,
        "offShift": 0.0,
        "defShift": 0.0,
    }


def _calc_off_def_v19_preview(attrs: List[int], pos: str, baselines: Dict[str, Any]) -> Tuple[float, float]:
    p = pos if pos in V19_POSITIONS else "SF"
    safe_attrs = _normalize_attrs(attrs)
    pos_mean = baselines.get("posMean") or {}
    pos_std = baselines.get("posStd") or {}
    abs_mean = baselines.get("absMean") or {}
    abs_std = baselines.get("absStd") or {}

    def z_pos(idx: int) -> float:
        return (_safe_float(safe_attrs[idx], 75.0) - _safe_float((pos_mean.get(p) or {}).get(idx), 75.0)) / _safe_std((pos_std.get(p) or {}).get(idx))

    def z_abs(idx: int) -> float:
        return (_safe_float(safe_attrs[idx], 75.0) - _safe_float(abs_mean.get(idx), 75.0)) / _safe_std(abs_std.get(idx))

    def z_to_rating(z: float) -> float:
        return _clamp_float(75 + 12 * z, 50, 99)

    abs_mix = {"PF": 0.70, "SF": 0.20, "PG": 0.10, "SG": 0.10, "C": 0.10}
    off_weights = _v19_pf_bridged_weights() if p == "PF" else OFF_WEIGHTS_POSZ.get(p, OFF_WEIGHTS_POSZ["SF"])

    z_pos_sum = 0.0
    z_abs_sum = 0.0
    for idx, weight in off_weights.items():
        z_pos_sum += float(weight) * z_pos(idx)
        z_abs_sum += float(weight) * z_abs(idx)

    mix = abs_mix.get(p, 0.10)
    off = z_to_rating((1 - mix) * z_pos_sum + mix * z_abs_sum)

    t3_gap = max(0.0, 50 - _safe_float(safe_attrs[T3], 0.0) - 2)
    close_gap = max(0.0, 60 - _safe_float(safe_attrs[CLOSE], 0.0) - 2)
    off -= min(6.0, 0.07 * _three_penalty_mult(p) * t3_gap)
    off -= min(6.0, 0.07 * _close_penalty_mult(p) * close_gap)
    off = _clamp_float(off, 50, 99)

    def_weights = DEF_WEIGHTS_POSZ.get(p, {})
    zsum_d = 0.0
    for idx, weight in def_weights.items():
        zsum_d += float(weight) * z_pos(idx)
    defense = z_to_rating(zsum_d)

    athleticism = _safe_float(safe_attrs[ATH], 75.0)
    abs_pen = max(0.0, 78 - athleticism) * 0.08
    rel_pen = max(0.0, _safe_float((pos_mean.get(p) or {}).get(ATH), 75.0) - athleticism) * 0.05

    if p == "SF":
        abs_pen *= 0.8
        rel_pen *= 0.8
        defense += 2.5
        perimeter_d = _safe_float(safe_attrs[PERD], 75.0)
        interior_d = _safe_float(safe_attrs[INTD], 75.0)
        high = max(perimeter_d, interior_d)
        low = min(perimeter_d, interior_d)
        if perimeter_d >= 88 and interior_d >= 88:
            defense += min(1.0, (min(perimeter_d, interior_d) - 88) * 0.05)
        tier = 0.0
        if perimeter_d >= 90 or interior_d >= 90:
            tier += 0.5
        if perimeter_d >= 85 and interior_d >= 85:
            tier += 0.5
        if high >= 93 and low >= 84:
            tier += 0.5
        if high >= 94 and low >= 90:
            tier += 0.5
        defense += min(2.0, tier)

    defense -= min(4.0, abs_pen + rel_pen)
    def_cap = 99 if p == "C" else 98 if p == "PF" else 96
    defense = _clamp_float(defense, 50, def_cap)

    return off, defense


def _build_v19_rating_baselines(league: Dict[str, Any]) -> Dict[str, Any]:
    players = _league_players_for_v19_baselines(league or {})
    baselines = _empty_v19_baselines()

    pos_buckets = {
        pos: {idx: [] for idx in set(V19_OFF_IDX + V19_DEF_IDX)}
        for pos in V19_POSITIONS
    }
    abs_buckets = {idx: [] for idx in V19_OFF_IDX}

    for player in players:
        pos = str(player.get("pos") or player.get("position") or "SF").upper()
        if pos not in V19_POSITIONS:
            pos = "SF"
        attrs = _normalize_attrs(player.get("attrs") or player.get("attributes") or [])

        # This mirrors LeagueEditor.jsx, including the duplicate SPD/ATH pushes
        # created by concatenating offensive and defensive index arrays.
        for idx in V19_OFF_IDX + V19_DEF_IDX:
            pos_buckets[pos][idx].append(_safe_float(attrs[idx], 75.0))
        for idx in V19_OFF_IDX:
            abs_buckets[idx].append(_safe_float(attrs[idx], 75.0))

    for pos in V19_POSITIONS:
        for idx in set(V19_OFF_IDX + V19_DEF_IDX):
            arr = pos_buckets[pos][idx]
            baselines["posMean"][pos][idx] = sum(arr) / len(arr) if arr else 75.0
            baselines["posStd"][pos][idx] = _sample_std(arr) if arr else 1.0

    for idx in V19_OFF_IDX:
        arr = abs_buckets[idx]
        baselines["absMean"][idx] = sum(arr) / len(arr) if arr else 75.0
        baselines["absStd"][idx] = _sample_std(arr) if arr else 1.0

    if not players:
        return baselines

    sum_ovr = 0.0
    sum_off = 0.0
    sum_def = 0.0
    n = 0
    for player in players:
        pos = str(player.get("pos") or player.get("position") or "SF").upper()
        if pos not in V19_POSITIONS:
            pos = "SF"
        attrs = _normalize_attrs(player.get("attrs") or player.get("attributes") or [])
        sum_ovr += _calc_overall_from_attrs(attrs, pos)
        off_preview, def_preview = _calc_off_def_v19_preview(attrs, pos, baselines)
        sum_off += off_preview
        sum_def += def_preview
        n += 1

    if n:
        ov_mean = sum_ovr / n
        off_mean = sum_off / n
        def_mean = sum_def / n
        baselines["offShift"] = _clamp_float(ov_mean - off_mean, -1.5, 1.5)
        baselines["defShift"] = _clamp_float(ov_mean - def_mean, -1.5, 1.5)

    return baselines


def _calc_off_def_v19(
    attrs: List[int],
    pos: str,
    name: str = "",
    height: Any = 78,
    baselines: Optional[Dict[str, Any]] = None,
) -> Tuple[int, int]:
    # height is accepted for exact call-shape parity with LeagueEditor.jsx. The
    # current v19 formula does not use height directly.
    _ = height
    p = str(pos or "SF").upper()
    if p not in V19_POSITIONS:
        p = "SF"

    safe_attrs = _normalize_attrs(attrs)
    active_baselines = baselines or _empty_v19_baselines()
    off, defense = _calc_off_def_v19_preview(safe_attrs, p, active_baselines)

    jitter = _v19_jitter(name, safe_attrs)
    off = _clamp_float(off + _safe_float(active_baselines.get("offShift"), 0.0) + jitter, 50, 99)
    def_cap = 99 if p == "C" else 98 if p == "PF" else 96
    defense = _clamp_float(
        defense + _safe_float(active_baselines.get("defShift"), 0.0) + 0.7 * jitter,
        50,
        def_cap,
    )

    return _bankers_round_js(off), _bankers_round_js(defense)



def _explode_v19(value: Any, power: float) -> float:
    return (_safe_float(value, 0.0) / 100.0) ** power


def _close_penalty_v19(close: Any) -> float:
    close_value = _safe_float(close, 0.0)
    if close_value >= 70:
        return 0.0
    return ((70 - close_value) / 30.0) ** 2.3


def _calc_scoring_rating_v19(pos: str, three: Any, mid: Any, close: Any) -> float:
    p = str(pos or "SF").upper()
    three_value = _safe_float(three, 75.0)
    mid_value = _safe_float(mid, 75.0)
    close_value = _safe_float(close, 75.0)

    if p in {"PG", "SG"}:
        three_term = _explode_v19(three_value, 7) * 1.2
        mid_term = _explode_v19(mid_value, 7) * 1.55
        close_term = _explode_v19(close_value, 6) * 1.1
        base = 0.38 * (three_value / 100.0) + 0.4 * (mid_value / 100.0) + 0.22 * (close_value / 100.0)
        penalty = _close_penalty_v19(close_value) * 1.7
        raw = base + three_term + mid_term + close_term - penalty
        return float(raw * 14.75 + 43.5)

    if p == "SF":
        three_term = _explode_v19(three_value, 7) * 1.05
        mid_term = _explode_v19(mid_value, 7) * 1.4
        close_term = _explode_v19(close_value, 7) * 1.5
        base = 0.32 * (three_value / 100.0) + 0.35 * (mid_value / 100.0) + 0.33 * (close_value / 100.0)
        penalty = _close_penalty_v19(close_value) * 1.2
        raw = base + three_term + mid_term + close_term - penalty
        return float(raw * 14.75 + 43.5)

    if p in {"PF", "C"}:
        close_term = _explode_v19(close_value, 8) * 1.95
        mid_term = _explode_v19(mid_value, 6) * 1.3
        three_term = _explode_v19(three_value, 5) * 0.6
        base = 0.58 * (close_value / 100.0) + 0.27 * (mid_value / 100.0) + 0.15 * (three_value / 100.0)
        penalty = _close_penalty_v19(close_value) * 2.0
        raw = base + three_term + mid_term + close_term - penalty
        return float(raw * 14.75 + 43.5)

    return 50.0


def _ratings_from_attrs(attrs: List[int]) -> Tuple[int, int]:
    if len(attrs) < 15:
        return 70, 70
    off_indices = [0, 1, 2, 3, 4, 5, 7, 13]
    def_indices = [8, 9, 10, 11, 12, 14]
    off_rating = round(sum(attrs[i] for i in off_indices) / len(off_indices))
    def_rating = round(sum(attrs[i] for i in def_indices) / len(def_indices))
    return int(off_rating), int(def_rating)


def _normalize_draft_prospect_for_state(
    prospect: Dict[str, Any],
    season_year: int,
    index: int = 0,
    rating_baselines: Optional[Dict[str, Any]] = None,
) -> Dict[str, Any]:
    p = copy.deepcopy(prospect or {})
    raw_attrs = p.get("attrs") if isinstance(p.get("attrs"), list) else p.get("attributes")
    has_source_truth_attrs = _has_source_truth_attrs(raw_attrs)
    attrs = _normalize_attrs(raw_attrs or [])
    p["attrs"] = attrs

    if has_source_truth_attrs:
        source_truth_overall = _calc_overall_from_attrs(
            attrs,
            p.get("pos") or p.get("position") or "SF",
        )
        p["overall"] = source_truth_overall
        p["ovr"] = source_truth_overall
        p["rating"] = source_truth_overall
        p["overallSource"] = "attrs_source_truth"

    if not p.get("name") and p.get("playerName"):
        p["name"] = p.get("playerName")
    if not p.get("playerName") and p.get("name"):
        p["playerName"] = p.get("name")

    p.setdefault("draftClassYear", season_year)
    p.setdefault("seasonYear", season_year)
    p.setdefault("draftProjection", _safe_int(p.get("trueRank") or p.get("rank"), index + 1))
    p.setdefault("trueRank", _safe_int(p.get("draftProjection") or p.get("rank"), index + 1))

    if p.get("id") is None or p.get("id") == "":
        p["id"] = f"custom_{season_year}_{str(index + 1).zfill(3)}_{_safe_id_text(p.get('name') or p.get('playerName') or 'prospect')}"

    if p.get("overall") is None and p.get("ovr") is None and p.get("rating") is None:
        p["overall"] = _resolve_prospect_overall(p, 65)

    overall = _resolve_prospect_overall(p, 65)
    if p.get("potential") is None or _safe_int(p.get("potential"), 0) <= 0:
        p["potential"] = overall
    else:
        p["potential"] = max(overall, _safe_int(p.get("potential"), overall))

    off_rating, def_rating = _calc_off_def_v19(
        attrs,
        p.get("pos") or p.get("position") or "SF",
        p.get("name") or p.get("playerName") or "",
        p.get("height") or 78,
        rating_baselines,
    )
    p["offRating"] = off_rating
    p["defRating"] = def_rating
    p["offense"] = off_rating
    p["defense"] = def_rating
    p["offRatingSource"] = "v19_attrs_source_truth"
    p["defRatingSource"] = "v19_attrs_source_truth"
    p["stamina"] = _calc_stamina_from_attrs(p.get("age"), attrs)
    p["staminaSource"] = "attrs_age_source_truth"
    p["scoringRating"] = _calc_scoring_rating_v19(
        p.get("pos") or p.get("position") or "SF",
        attrs[T3],
        attrs[MID],
        attrs[CLOSE],
    )
    p["scoringRatingSource"] = "v19_attrs_source_truth"

    source = (
        p.get("college") or p.get("school") or p.get("university") or p.get("academy") or
        p.get("academyName") or p.get("sourceName") or p.get("draftSource") or ""
    )
    if source:
        p.setdefault("college", source)
        p.setdefault("school", source)
        p.setdefault("academy", source)
        p.setdefault("academyName", source)
        p.setdefault("sourceName", source)
        p.setdefault("draftSource", source)

    return p


def _prospect_to_player(
    prospect: Dict[str, Any],
    team_name: str,
    pick: Optional[Dict[str, Any]],
    season_year: int,
    drafted: bool = True,
    rating_baselines: Optional[Dict[str, Any]] = None,
) -> Dict[str, Any]:
    attrs = [int(x) for x in (prospect.get("attrs") or [])]
    while len(attrs) < 15:
        attrs.append(60)

    overall_rating = _resolve_prospect_overall(prospect, 65)
    off_rating, def_rating = _calc_off_def_v19(
        attrs,
        prospect.get("pos") or prospect.get("position") or "SF",
        prospect.get("name") or prospect.get("playerName") or "",
        prospect.get("height") or 78,
        rating_baselines,
    )

    overall_pick = _safe_int(pick.get("pick"), 0) if pick else None
    round_num = _safe_int(pick.get("round"), 0) if pick else None
    pick_in_round = _safe_int(pick.get("pickInRound"), 0) if pick else None
    player_id_suffix = _safe_id_text(prospect.get("name") or prospect.get("id") or "rookie")
    player_id = f"rookie_{season_year}_{overall_pick or 'udfa'}_{player_id_suffix}"

    contract = _rookie_contract(round_num, overall_pick or 60, season_year) if drafted and pick else {
        "startYear": season_year,
        "salaryByYear": [],
        "option": None,
    }

    rights_team = team_name if drafted else None
    acquired_via = "draft" if drafted else "undrafted_free_agent"
    contract_type = contract.get("type") if isinstance(contract, dict) else "standard"
    if drafted:
        roster_status = "rookie_pending"
        assignment_status = "unsigned_rookie"
        rookie_pending = True
    else:
        roster_status = "free_agent"
        assignment_status = "free_agent"
        rookie_pending = False

    transaction_label = (
        f"Drafted No. {overall_pick} by {team_name}"
        if drafted and overall_pick
        else "Entered free agency as an undrafted rookie"
    )

    portrait_url = _prospect_portrait_url(prospect)

    return {
        "id": player_id,
        "name": prospect.get("name") or "Generated Rookie",
        "pos": prospect.get("pos") or "SF",
        "secondaryPos": prospect.get("secondaryPos") or "",
        "age": _safe_int(prospect.get("age"), 20),
        "height": _safe_int(prospect.get("height"), 78),
        "weight": _safe_int(prospect.get("weight"), 215),
        "potential": max(overall_rating, _safe_int(prospect.get("potential"), overall_rating)),
        "overall": overall_rating,
        "offRating": off_rating,
        "defRating": def_rating,
        "offense": off_rating,
        "defense": def_rating,
        "offRatingSource": "v19_attrs_source_truth",
        "defRatingSource": "v19_attrs_source_truth",
        "stamina": _calc_stamina_from_attrs(_safe_int(prospect.get("age"), 20), attrs),
        "staminaSource": "attrs_age_source_truth",
        "scoringRating": _calc_scoring_rating_v19(
            prospect.get("pos") or prospect.get("position") or "SF",
            attrs[T3],
            attrs[MID],
            attrs[CLOSE],
        ),
        "scoringRatingSource": "v19_attrs_source_truth",
        "attrs": attrs,
        "portraitId": prospect.get("portraitId") or "",
        "headshot": portrait_url,
        "image": portrait_url,
        "img": portrait_url,
        "birthMonth": _safe_int(prospect.get("birthMonth"), 1),
        "birthDay": _safe_int(prospect.get("birthDay"), 1),
        "lastBirthdayYear": season_year,
        "team": team_name if drafted else "Free Agent",
        "contract": contract,
        "contractType": contract_type,
        "rosterStatus": roster_status,
        "assignmentStatus": assignment_status,
        "rookieSigningPending": rookie_pending,
        "rights": {
            "heldByTeam": rights_team,
            "seasonsTowardBird": 0,
            "birdLevel": "none" if not drafted else "non_bird",
            "rookieScale": bool(drafted and round_num == 1),
            "restrictedFreeAgent": False,
        },
        "meta": {
            "draftYear": season_year if drafted else None,
            "draftRound": round_num if drafted else None,
            "draftPick": overall_pick if drafted else None,
            "draftPickInRound": pick_in_round if drafted else None,
            "draftedBy": team_name if drafted else None,
            "acquiredVia": acquired_via,
            "rookieSigningPending": rookie_pending,
            "rookieSigningDecision": None,
            "proSeasons": 0,
            "yearsWithCurrentTeam": 0,
            "college": prospect.get("college") or "",
            "nationality": prospect.get("nationality") or "",
            "archetype": prospect.get("archetype") or "",
        },
        "history": {
            "seasons": [],
            "accolades": [],
            "transactions": [
                {
                    "seasonYear": season_year,
                    "type": acquired_via,
                    "label": transaction_label,
                    "teamName": team_name if drafted else "Free Agent",
                }
            ],
        },
    }


def _add_player_to_team(league: Dict[str, Any], team_name: str, player: Dict[str, Any]) -> bool:
    team = _find_team(league, team_name)
    if not team:
        return False
    if not isinstance(team.get("players"), list):
        team["players"] = []
    if not isinstance(team.get("pendingRookieSignings"), list):
        team["pendingRookieSignings"] = []

    player_id = player.get("id")
    contract = player.get("contract") if isinstance(player.get("contract"), dict) else {}
    contract_type = str(player.get("contractType") or contract.get("type") or "standard").lower()
    is_pending_rookie = bool(
        player.get("rookieSigningPending")
        or contract.get("pendingRookieSigning")
        or contract_type in {"unsigned_rookie", "rookie_pending"}
    )

    target = team["pendingRookieSignings"] if is_pending_rookie else team["players"]
    existing_ids = {p.get("id") for p in target if isinstance(p, dict)}
    if player_id not in existing_ids:
        target.append(player)
    return True


def _add_undrafted_to_free_agents(league: Dict[str, Any], state: Dict[str, Any], season_year: int) -> None:
    if state.get("undraftedProcessed"):
        return

    available = state.get("availableProspects") or []
    if not isinstance(league.get("freeAgents"), list):
        league["freeAgents"] = []

    existing_ids = {p.get("id") for p in league["freeAgents"] if isinstance(p, dict)}
    undrafted_added = []
    rating_baselines = _build_v19_rating_baselines(league)

    # Keep the top remaining undrafted prospects available. This gives the FA pool life
    # without bloating localStorage too aggressively.
    for prospect in available[:24]:
        player = _prospect_to_player(
            prospect,
            "Free Agent",
            None,
            season_year,
            drafted = False,
            rating_baselines = rating_baselines,
        )
        if player.get("id") in existing_ids:
            continue
        league["freeAgents"].append(player)
        existing_ids.add(player.get("id"))
        undrafted_added.append({
            "playerId": player.get("id"),
            "playerName": player.get("name"),
            "overall": player.get("overall"),
            "potential": player.get("potential"),
            "pos": player.get("pos"),
        })

    state["undraftedProcessed"] = True
    state["undraftedAddedToFreeAgency"] = undrafted_added


def _normalize_draft_order(draft_order: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    out = []
    for idx, row in enumerate(draft_order or []):
        if not isinstance(row, dict):
            continue
        pick = _safe_int(row.get("pick"), idx + 1)
        round_num = _safe_int(row.get("round"), 1 if pick <= 30 else 2)
        pick_in_round = _safe_int(row.get("pickInRound"), pick if round_num == 1 else pick - 30)
        team_name = row.get("currentOwnerTeamName") or row.get("teamName") or row.get("originalTeamName")
        out.append({
            **row,
            "pick": pick,
            "round": round_num,
            "pickInRound": pick_in_round,
            "teamName": row.get("teamName") or team_name,
            "currentOwnerTeamName": team_name,
            "originalTeamName": row.get("originalTeamName") or row.get("teamName") or team_name,
        })
    return sorted(out, key = lambda r: _safe_int(r.get("pick"), 999))


def initialize_draft(league_data: Dict[str, Any], payload: Dict[str, Any]) -> Dict[str, Any]:
    league = copy.deepcopy(_plain(league_data) or {})
    payload = _plain(payload) or {}
    season_year = _get_season_year(league, payload)

    existing_state = payload.get("draftState") or (league.get("draftState") or {}).get("draft")
    existing_state_season = _safe_int(existing_state.get("seasonYear"), 0) if isinstance(existing_state, dict) else 0
    if (
        isinstance(existing_state, dict)
        and existing_state_season == season_year
        and existing_state.get("draftOrder")
        and existing_state.get("availableProspects")
    ):
        return {
            "ok": True,
            "version": DRAFT_LOGIC_VERSION,
            "leagueData": league,
            "draftState": existing_state,
            "message": "Loaded existing draft state.",
        }

    draft_order = payload.get("draftOrder") or (league.get("draftState") or {}).get("draftOrder") or []
    draft_order = _normalize_draft_order(draft_order)

    if not draft_order:
        return {
            "ok": False,
            "reason": "MISSING_DRAFT_ORDER",
            "version": DRAFT_LOGIC_VERSION,
            "leagueData": league,
        }

    draft_class = payload.get("draftClass") or (league.get("draftState") or {}).get("draftClass")
    if isinstance(draft_class, list) and draft_class and not _draft_class_matches_season(draft_class, season_year):
        draft_class = None

    if not isinstance(draft_class, list) or len(draft_class) < len(draft_order):
        count = max(84, len(draft_order) + 24)
        class_seed = payload.get("classSeed")
        if class_seed is None:
            class_seed = _stable_seed("draft_class", season_year)

        if generate_draft_class is None:
            return {
                "ok": False,
                "reason": "DRAFT_CLASS_GENERATOR_NOT_AVAILABLE",
                "version": DRAFT_LOGIC_VERSION,
                "leagueData": league,
            }

        generated = generate_draft_class(
            season_year = season_year,
            count = count,
            seed = _safe_int(class_seed, _stable_seed("draft_class", season_year)),
        )
        draft_class = generated.get("draftClass") or []
        class_meta = generated.get("classMeta") or {}
    else:
        class_meta = {
            "seasonYear": season_year,
            "summary": "Loaded provided draft class",
            "prospectCount": len(draft_class),
        }

    rating_baselines = _build_v19_rating_baselines(league)
    draft_class = [
        _normalize_draft_prospect_for_state(p, season_year, index, rating_baselines)
        for index, p in enumerate(draft_class or [])
    ]

    available = sorted(
        [copy.deepcopy(p) for p in draft_class],
        key = lambda p: (_safe_int(p.get("draftProjection"), 999), _safe_int(p.get("trueRank"), 999)),
    )

    state = {
        "version": DRAFT_LOGIC_VERSION,
        "seasonYear": season_year,
        "currentPickIndex": 0,
        "draftOrder": draft_order,
        "draftClass": draft_class,
        "classMeta": class_meta,
        "availableProspects": available,
        "draftedPicks": [],
        "completed": False,
        "undraftedProcessed": False,
        "undraftedAddedToFreeAgency": [],
    }

    league.setdefault("draftState", {})
    league["draftState"]["draft"] = state
    league["draftState"]["draftClass"] = draft_class

    return {
        "ok": True,
        "version": DRAFT_LOGIC_VERSION,
        "leagueData": league,
        "draftState": state,
        "message": "Initialized draft.",
    }


def _select_prospect_by_id(available: List[Dict[str, Any]], prospect_id: str) -> Optional[Dict[str, Any]]:
    for prospect in available:
        if prospect.get("id") == prospect_id:
            return prospect
    return None


def _complete_if_needed(league: Dict[str, Any], state: Dict[str, Any], season_year: int) -> None:
    if _safe_int(state.get("currentPickIndex"), 0) >= len(state.get("draftOrder") or []):
        state["completed"] = True
        _add_undrafted_to_free_agents(league, state, season_year)


def make_pick(
    league_data: Dict[str, Any],
    payload: Dict[str, Any],
    force_cpu: bool = False,
) -> Dict[str, Any]:
    league = copy.deepcopy(_plain(league_data) or {})
    payload = _plain(payload) or {}
    season_year = _get_season_year(league, payload)

    state = copy.deepcopy(payload.get("draftState") or (league.get("draftState") or {}).get("draft") or {})
    if not state or not state.get("draftOrder"):
        init = initialize_draft(league, payload)
        if not init.get("ok"):
            return init
        state = init.get("draftState") or {}
        league = init.get("leagueData") or league

    if state.get("completed"):
        _complete_if_needed(league, state, season_year)
        league.setdefault("draftState", {})["draft"] = state
        return {
            "ok": True,
            "version": DRAFT_LOGIC_VERSION,
            "leagueData": league,
            "draftState": state,
            "message": "Draft already complete.",
        }

    current_index = _safe_int(state.get("currentPickIndex"), 0)
    draft_order = state.get("draftOrder") or []
    if current_index >= len(draft_order):
        _complete_if_needed(league, state, season_year)
        league.setdefault("draftState", {})["draft"] = state
        return {
            "ok": True,
            "version": DRAFT_LOGIC_VERSION,
            "leagueData": league,
            "draftState": state,
            "message": "Draft complete.",
        }

    pick = draft_order[current_index]
    team_name = pick.get("currentOwnerTeamName") or pick.get("teamName")
    user_team_name = payload.get("userTeamName")
    prospect_id = payload.get("prospectId")
    available = state.get("availableProspects") or []

    if not available:
        state["completed"] = True
        _complete_if_needed(league, state, season_year)
        league.setdefault("draftState", {})["draft"] = state
        return {
            "ok": True,
            "version": DRAFT_LOGIC_VERSION,
            "leagueData": league,
            "draftState": state,
            "message": "No available prospects remaining.",
        }

    is_user_pick = bool(user_team_name and team_name == user_team_name and not force_cpu)

    if is_user_pick:
        if not prospect_id:
            return {
                "ok": False,
                "reason": "USER_PICK_REQUIRES_PROSPECT_ID",
                "version": DRAFT_LOGIC_VERSION,
                "leagueData": league,
                "draftState": state,
            }
        prospect = _select_prospect_by_id(available, prospect_id)
        if not prospect:
            return {
                "ok": False,
                "reason": "PROSPECT_NOT_AVAILABLE",
                "version": DRAFT_LOGIC_VERSION,
                "leagueData": league,
                "draftState": state,
            }
    else:
        prospect = _choose_cpu_prospect(state, league, pick, season_year)

    rating_baselines = _build_v19_rating_baselines(league)
    player = _prospect_to_player(
        prospect,
        team_name,
        pick,
        season_year,
        drafted = True,
        rating_baselines = rating_baselines,
    )
    added = _add_player_to_team(league, team_name, player)

    pick_record = {
        "pick": pick.get("pick"),
        "round": pick.get("round"),
        "pickInRound": pick.get("pickInRound"),
        "teamName": team_name,
        "originalTeamName": pick.get("originalTeamName") or team_name,
        "playerId": player.get("id"),
        "playerName": player.get("name"),
        "pos": player.get("pos"),
        "overall": player.get("overall"),
        "potential": player.get("potential"),
        "age": player.get("age"),
        "portraitId": player.get("portraitId") or "",
        "headshot": player.get("headshot") or "",
        "image": player.get("image") or "",
        "img": player.get("img") or "",
        "archetype": prospect.get("archetype") or "",
        "tier": prospect.get("tier") or "",
        "userControlled": bool(is_user_pick),
        "addedToRoster": bool(added),
    }

    state["draftedPicks"] = (state.get("draftedPicks") or []) + [pick_record]
    state["availableProspects"] = [p for p in available if p.get("id") != prospect.get("id")]
    state["currentPickIndex"] = current_index + 1

    _complete_if_needed(league, state, season_year)

    league.setdefault("draftState", {})
    league["draftState"]["draft"] = state
    league["draftState"]["draftComplete"] = bool(state.get("completed"))

    return {
        "ok": True,
        "version": DRAFT_LOGIC_VERSION,
        "leagueData": league,
        "draftState": state,
        "pick": pick_record,
    }


def sim_until_user_pick(league_data: Dict[str, Any], payload: Dict[str, Any]) -> Dict[str, Any]:
    league = copy.deepcopy(_plain(league_data) or {})
    payload = _plain(payload) or {}
    user_team_name = payload.get("userTeamName")
    season_year = _get_season_year(league, payload)

    state = copy.deepcopy(payload.get("draftState") or (league.get("draftState") or {}).get("draft") or {})
    if not state or not state.get("draftOrder"):
        init = initialize_draft(league, payload)
        if not init.get("ok"):
            return init
        league = init.get("leagueData") or league
        state = init.get("draftState") or {}

    picks_made = []
    safety = 0

    while not state.get("completed") and safety < 80:
        safety += 1
        idx = _safe_int(state.get("currentPickIndex"), 0)
        order = state.get("draftOrder") or []
        if idx >= len(order):
            break
        current = order[idx]
        team_name = current.get("currentOwnerTeamName") or current.get("teamName")

        if user_team_name and team_name == user_team_name:
            break

        result = make_pick(league, {**payload, "draftState": state, "userTeamName": user_team_name}, force_cpu = True)
        if not result.get("ok"):
            return result
        league = result.get("leagueData") or league
        state = result.get("draftState") or state
        if result.get("pick"):
            picks_made.append(result.get("pick"))

    league.setdefault("draftState", {})["draft"] = state
    return {
        "ok": True,
        "version": DRAFT_LOGIC_VERSION,
        "leagueData": league,
        "draftState": state,
        "picksMade": picks_made,
        "message": "Simmed to user pick or draft completion.",
    }


