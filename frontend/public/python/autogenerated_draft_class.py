def _apply_distribution_noise(counts: Dict[int, int], limits: Dict[int, Tuple[int, int]], rng: random.Random) -> None:
    # Small adjacent noise prevents every normal class from having the exact same histogram.
    possible_overalls = list(range(MIN_GENERATED_DRAFT_OVR, MAX_GENERATED_DRAFT_OVR + 1))
    for _ in range(rng.randint(3, 7)):
        src = rng.choice(possible_overalls)
        dst = src + rng.choice([-1, 1])
        if dst < MIN_GENERATED_DRAFT_OVR or dst > MAX_GENERATED_DRAFT_OVR:
            continue
        _move_ovr_count(counts, limits, src, dst, 1)


def _normalize_count_template(counts: Dict[int, int], limits: Dict[int, Tuple[int, int]]) -> Dict[int, int]:
    # Keep all tracked OVRs present so summaries are predictable.
    normalized = {overall: int(counts.get(overall, 0)) for overall in range(MIN_GENERATED_DRAFT_OVR, MAX_GENERATED_DRAFT_OVR + 1)}

    # Safety clamp to declared limits, then restore the exact 100-player total by
    # moving counts between legal buckets. This should rarely do anything, but it
    # keeps the function robust if future template numbers are edited.
    for overall, (lo, hi) in limits.items():
        normalized[overall] = max(lo, min(hi, normalized.get(overall, 0)))

    total = sum(normalized.values())
    while total > DRAFT_CLASS_FIXED_COUNT:
        changed = False
        for overall in range(MIN_GENERATED_DRAFT_OVR + 1, MAX_GENERATED_DRAFT_OVR + 1):
            lo, _ = _count_limit(limits, overall)
            if normalized.get(overall, 0) > lo:
                normalized[overall] -= 1
                total -= 1
                changed = True
                break
        if not changed:
            break

    while total < DRAFT_CLASS_FIXED_COUNT:
        changed = False
        for overall in [66, 68, 69, 70, 67, 71, 72, 73, 74, 75, 76, 77, 78, 79, 80, 81, 82]:
            _, hi = _count_limit(limits, overall)
            if normalized.get(overall, 0) < hi:
                normalized[overall] += 1
                total += 1
                changed = True
                break
        if not changed:
            break

    return normalized


def _overall_slots_for_class(class_type: str, rng: random.Random) -> Tuple[List[int], Dict[str, Any]]:
    safe_class_type = _template_key_for_class_type(class_type)
    counts = dict(DRAFT_OVR_COUNT_TEMPLATES[safe_class_type])
    limits = DRAFT_OVR_COUNT_LIMITS[safe_class_type]

    shape_options = DRAFT_DISTRIBUTION_SHAPE_WEIGHTS.get(safe_class_type, DRAFT_DISTRIBUTION_SHAPE_WEIGHTS["normal"])
    shape_names = [name for name, _weight in shape_options]
    shape_weights = [weight for _name, weight in shape_options]
    shape = rng.choices(shape_names, weights = shape_weights)[0]

    _apply_distribution_shape(counts, limits, shape, rng)
    _apply_distribution_noise(counts, limits, rng)
    counts = _normalize_count_template(counts, limits)

    # Mandatory hard guardrails.
    if counts.get(MIN_GENERATED_DRAFT_OVR, 0) <= 0:
        raise ValueError("Draft histogram generated no 66 OVR players")
    if sum(counts.values()) != DRAFT_CLASS_FIXED_COUNT:
        raise ValueError(f"Draft histogram count mismatch: {sum(counts.values())}")

    slots: List[int] = []
    for overall in sorted(counts.keys(), reverse = True):
        slots.extend([overall] * counts[overall])

    return slots, {
        "shape": shape,
        "targetDistribution": {
            str(overall): counts[overall]
            for overall in sorted(counts.keys(), reverse = True)
            if counts[overall] > 0
        },
    }


def _overall_from_distribution(rank: int, total_count: int, class_type: str) -> int:
    # Fallback for older direct calls. Main generation uses _overall_slots_for_class.
    safe_class_type = _template_key_for_class_type(class_type)
    counts = _normalize_count_template(dict(DRAFT_OVR_COUNT_TEMPLATES[safe_class_type]), DRAFT_OVR_COUNT_LIMITS[safe_class_type])
    slots: List[int] = []
    for overall in sorted(counts.keys(), reverse = True):
        slots.extend([overall] * counts[overall])
    index = _clamp(max(1, _safe_int(rank, 1)), 1, len(slots)) - 1
    return slots[index]


def _overall_distribution_summary(prospects: List[Dict[str, Any]]) -> Dict[str, int]:
    counts: Dict[str, int] = {}
    for prospect in prospects or []:
        overall = str(_safe_int((prospect or {}).get("overall"), 0))
        if overall == "0":
            continue
        counts[overall] = counts.get(overall, 0) + 1

    return {
        key: counts[key]
        for key in sorted(counts.keys(), key = lambda value: int(value), reverse = True)
    }


def _tier_label_for_rank(rank: int, class_type: str, overall: int, potential: int) -> str:
    # Keep the word "Generational" special. It should only appear for the true
    # rare ceiling outcome, not every 80/97 or 81/97 top pick.
    if rank == 1 and overall >= 82 and potential >= 98:
        return "Generational"
    if rank <= 4 and potential >= 92:
        return "Franchise"
    if rank <= 8:
        return "High Lottery"
    if rank <= 14:
        return "Lottery"
    if rank <= 30:
        return "First Round"
    if rank <= 45:
        return "Late First / Early Second"
    if rank <= 60:
        return "Second Round"
    return "Undrafted"


def _max_overall_for_rank(rank: int, class_type: str) -> int:
    if rank == 1 and class_type == "generational":
        return 82
    if rank == 1 and class_type in {"star_heavy", "normal", "deep", "defensive_class", "guard_heavy", "big_man_heavy"}:
        return 81
    if rank <= 3 and class_type in {"star_heavy", "generational"}:
        return 80
    if rank <= 5:
        return 79
    if rank <= 8 and class_type in {"star_heavy", "generational"}:
        return 78
    if rank <= 10:
        return 77
    return MAX_GENERATED_DRAFT_OVR


def _potential_cap_for_rank(rank: int, class_type: str) -> int:
    if rank == 1 and class_type == "generational":
        return 98
    if rank == 1 and class_type == "weak":
        return 95
    if rank == 1:
        return 97
    if rank <= 3:
        if class_type == "generational":
            return 97
        if class_type == "weak":
            return 95
        return 96
    if rank <= 5:
        return 96 if class_type in {"generational", "star_heavy", "normal", "deep"} else 94
    if rank <= 10:
        return 94 if class_type != "weak" else 92
    if rank <= 14:
        return 91
    if rank <= 30:
        return 89
    if rank <= 45:
        return 86
    if rank <= 60:
        return 84
    return 82


def _overall_jitter_for_rank(rank: int, rng: random.Random) -> float:
    if rank <= 3:
        spread = 0.45
    elif rank <= 10:
        spread = 0.75
    elif rank <= 30:
        spread = 0.95
    else:
        spread = 0.85
    return rng.triangular(-spread, spread, 0.0)


def _age_for_rank(rank: int, class_type: str, rng: random.Random) -> int:
    # Top prospects skew younger. Later prospects skew older/NBA-ready.
    # This lets two same-OVR prospects separate naturally by upside.
    if rank <= 10:
        ages = [18, 19, 20, 21, 22, 23]
        weights = [0.06, 0.70, 0.20, 0.04, 0.00, 0.00]
    elif rank <= 30:
        ages = [18, 19, 20, 21, 22, 23]
        weights = [0.04, 0.42, 0.30, 0.14, 0.07, 0.03]
    elif rank <= 60:
        ages = [18, 19, 20, 21, 22, 23]
        weights = [0.02, 0.22, 0.27, 0.21, 0.18, 0.10]
    else:
        ages = [18, 19, 20, 21, 22, 23]
        weights = [0.01, 0.13, 0.19, 0.24, 0.24, 0.19]

    if class_type == "deep" and rank >= 30:
        # Deep classes can include a few more young second-round upside swings.
        weights = [weights[0] + 0.01, weights[1] + 0.04, weights[2], max(0.0, weights[3] - 0.01), max(0.0, weights[4] - 0.02), max(0.0, weights[5] - 0.02)]

    return rng.choices(ages, weights = weights)[0]


def _age_potential_adjustment(age: int, rng: random.Random) -> int:
    if age <= 18:
        return rng.randint(2, 4)
    if age == 19:
        return rng.randint(1, 3)
    if age == 20:
        return rng.randint(0, 2)
    if age == 21:
        return rng.randint(-1, 1)
    if age == 22:
        return rng.randint(-2, 0)
    return rng.randint(-3, -1)


def _potential_noise_for_rank(rank: int, rng: random.Random) -> int:
    if rank <= 5:
        return rng.choice([-1, 0, 0, 1, 1, 2])
    if rank <= 14:
        return rng.choice([-2, -1, 0, 0, 1, 2])
    if rank <= 60:
        return rng.choice([-2, -1, 0, 0, 1, 1])
    return rng.choice([-3, -2, -1, 0, 0, 1])


def _talent_for_rank(
    rank: int,
    class_type: str,
    age: int,
    rng: random.Random,
    total_count: int = DRAFT_CLASS_FIXED_COUNT,
    overall_slot: Optional[int] = None,
) -> Tuple[str, int, int]:
    safe_class_type = _template_key_for_class_type(class_type)

    # v11: OVR is assigned from the pre-built class histogram. This keeps the
    # full draft distribution stable while still allowing class personality.
    if overall_slot is None:
        overall = _overall_from_distribution(rank, total_count, safe_class_type)
    else:
        overall = _safe_int(overall_slot, MIN_GENERATED_DRAFT_OVR)

    overall = _clamp(
        overall,
        MIN_GENERATED_DRAFT_OVR,
        _max_overall_for_rank(rank, safe_class_type),
    )

    base_gap = _linear_anchor_value(rank, DRAFT_POT_GAP_CURVES[safe_class_type])
    raw_potential = overall + base_gap + _age_potential_adjustment(age, rng) + _potential_noise_for_rank(rank, rng)
    potential = _clamp(
        raw_potential,
        overall,
        min(MAX_GENERATED_DRAFT_POTENTIAL, _potential_cap_for_rank(rank, safe_class_type)),
    )

    # Preserve the requested rare ceiling: the only normal 82 OVR outcome is a
    # generational #1, and that player should carry 98 POT.
    if rank == 1 and safe_class_type == "generational" and overall >= 82:
        potential = 98

    # 81/97 is elite franchise talent, but not labeled generational.
    if rank == 1 and safe_class_type == "generational" and overall < 82:
        potential = min(potential, 97)

    tier = _tier_label_for_rank(rank, safe_class_type, overall, potential)
    return tier, overall, potential

# Backwards-compatible helper names kept for any older imports/tests that may call them directly.
def _tier_for_rank(rank: int, class_type: str, rng: random.Random) -> Tuple[str, Tuple[int, int], Tuple[int, int]]:
    age = _age_for_rank(rank, class_type, rng)
    tier, overall, potential = _talent_for_rank(rank, class_type, age, rng, DRAFT_CLASS_FIXED_COUNT)
    return tier, (overall, overall), (potential, potential)


def _adjust_for_class_type(overall: int, potential: int, rank: int, class_type: str) -> Tuple[int, int]:
    return (
        _clamp(overall, MIN_GENERATED_DRAFT_OVR, MAX_GENERATED_DRAFT_OVR),
        _clamp(potential, max(MIN_GENERATED_DRAFT_OVR, overall), MAX_GENERATED_DRAFT_POTENTIAL),
    )


def _build_attrs(
    rng: random.Random,
    pos: str,
    archetype: str,
    overall: int,
    potential: int,
    class_type: str,
) -> List[int]:
    bias = ARCHETYPE_BIASES.get(archetype, [0] * 15)
    base = overall - 2

    attrs = []
    for idx, b in enumerate(bias):
        noise = rng.randint(-6, 6)
        value = base + b + noise
        attrs.append(_clamp(value, 25, 99))

    if pos == "PG":
        attrs[9] = min(attrs[9], overall - 5)
        attrs[10] = min(attrs[10], overall - 10)
        attrs[12] = min(attrs[12], overall - 5)
    elif pos == "C":
        attrs[4] = min(attrs[4], overall + 1)
        attrs[6] = min(attrs[6], overall)
        attrs[8] = min(attrs[8], overall + 2)

    if class_type == "defensive_class":
        for idx in [8, 9, 10, 11, 12, 14]:
            attrs[idx] = _clamp(attrs[idx] + rng.randint(2, 6), 25, 99)

    # Make true stars feel a bit cleaner across IQ and stamina-adjacent skills.
    if potential >= 92:
        attrs[13] = _clamp(attrs[13] + rng.randint(2, 5), 25, 99)
        attrs[14] = _clamp(attrs[14] + rng.randint(1, 4), 25, 99)

    return attrs


def _ratings_from_attrs(attrs: List[int]) -> Tuple[int, int, float]:
    off_indices = [0, 1, 2, 3, 4, 5, 7, 13]
    def_indices = [8, 9, 10, 11, 12, 14]

    off_rating = round(sum(attrs[i] for i in off_indices) / len(off_indices))
    def_rating = round(sum(attrs[i] for i in def_indices) / len(def_indices))

    scoring_rating = (
        attrs[0] * 0.28
        + attrs[1] * 0.21
        + attrs[2] * 0.24
        + attrs[4] * 0.10
        + attrs[7] * 0.08
        + attrs[13] * 0.09
    )

    return int(off_rating), int(def_rating), float(scoring_rating)


def _weighted_profile_choice(rng: random.Random) -> Dict[str, Any]:
    total = sum(float(profile.get("weight", 0.0)) for profile in IDENTITY_PROFILES)
    ticket = rng.random() * total
    running = 0.0

    for profile in IDENTITY_PROFILES:
        running += float(profile.get("weight", 0.0))
        if ticket <= running:
            return dict(profile)

    return dict(IDENTITY_PROFILES[0])


def _weighted_value_choice(rng: random.Random, weighted: List[Tuple[str, float]], fallback: str = "") -> str:
    if not weighted:
        return fallback

    total = sum(float(weight) for _, weight in weighted)
    ticket = rng.random() * total
    running = 0.0

    for item, weight in weighted:
        running += float(weight)
        if ticket <= running:
            return item

    return weighted[-1][0]


def _make_identity_profile(rng: random.Random) -> Dict[str, Any]:
    profile = _weighted_profile_choice(rng)
    profile_key = profile.get("key") or "unknown"
    nationality = _weighted_value_choice(rng, profile.get("nationalityWeights") or [], "USA")
    college_bucket = _weighted_value_choice(rng, profile.get("collegeWeights") or [], "college")
    college_options = COLLEGE_GROUPS.get(college_bucket, COLLEGES)

    first_names = _unique_text_list(
        list(profile.get("firstNames") or FIRST_NAMES)
        + list(ADDITIONAL_FIRST_NAMES_BY_PROFILE.get(profile_key) or [])
        + list(MEGA_FIRST_NAMES_BY_PROFILE.get(profile_key) or [])
    )
    last_names = _unique_text_list(
        list(profile.get("lastNames") or LAST_NAMES)
        + list(ADDITIONAL_LAST_NAMES_BY_PROFILE.get(profile_key) or [])
        + list(MEGA_LAST_NAMES_BY_PROFILE.get(profile_key) or [])
    )

    return {
        "identityKey": profile_key,
        "ethnicityGroup": profile.get("ethnicityGroup") or "other",
        "subgroup": profile.get("subgroup") or "other",
        "skinTone": profile.get("skinTone") or "medium",
        "region": profile.get("region") or "unknown",
        "nationality": nationality,
        "collegeBucket": college_bucket,
        "college": rng.choice(college_options),
        "firstNames": first_names or FIRST_NAMES,
        "lastNames": last_names or LAST_NAMES,
        "portraitRange": profile.get("portraitRange") or (1, ROOKIE_FACE_COUNT),
    }


def _make_name(rng: random.Random, used: set, identity_profile: Optional[Dict[str, Any]] = None) -> str:
    first_names = (identity_profile or {}).get("firstNames") or FIRST_NAMES
    last_names = (identity_profile or {}).get("lastNames") or LAST_NAMES

    # Try a lot harder than before so generated classes do not keep recycling
    # familiar combinations like Lamar Thomas or Bryce Bennett.
    for _ in range(500):
        name = f"{rng.choice(first_names)} {rng.choice(last_names)}"
        if name not in used and name not in OVERUSED_FULL_NAMES:
            used.add(name)
            return name

    # Extremely rare fallback if a pool somehow gets exhausted.
    for suffix in ["Jr.", "II", "III"]:
        name = f"{rng.choice(first_names)} {rng.choice(last_names)} {suffix}"
        if name not in used and name not in OVERUSED_FULL_NAMES:
            used.add(name)
            return name

    name = f"{rng.choice(first_names)} {rng.choice(last_names)}"
    used.add(name)
    return name


def _rookie_face_id(index: int) -> str:
    return f"rookie_face_{index:04d}"


def _rookie_face_url(face_id: str) -> str:
    return f"{ROOKIE_FACE_BASE_URL}/{face_id}.png"


def _rookie_face_entry(face_id: str) -> Dict[str, str]:
    url = _rookie_face_url(face_id)
    return {
        "id": face_id,
        "url": url,
        "headshot": url,
        "image": url,
        "img": url,
    }


def _portrait_ids_for_range(start: int, end: int, portrait_count: int) -> List[str]:
    start = max(1, _safe_int(start, 1))
    end = min(_safe_int(end, portrait_count), portrait_count)
    if end < start:
        return []
    return [_rookie_face_id(i) for i in range(start, end + 1)]


def _all_portrait_ids(portrait_count: int) -> List[str]:
    return [_rookie_face_id(i) for i in range(1, portrait_count + 1)]


def _choose_portrait_for_identity(
    identity_profile: Dict[str, Any],
    rng: random.Random,
    used_portrait_ids: Optional[List[str]] = None,
    assigned_this_class: Optional[set] = None,
    portrait_count: int = ROOKIE_FACE_COUNT,
) -> Dict[str, str]:
    # Phase 1 safety: only use manually verified portrait IDs.
    # Do not fall back to the old 1-100 range because most of those files do not exist
    # or have not been confirmed as clean transparent PNGs yet.
    profile_key = (identity_profile or {}).get("identityKey") or ""
    matching = list(SAFE_ROOKIE_FACE_IDS_BY_PROFILE.get(profile_key) or [])
    all_safe_ids = list(SAFE_ROOKIE_FACE_IDS)

    if not all_safe_ids:
        return _rookie_face_entry(_rookie_face_id(1))

    # If a profile has no verified bucket yet, use the full safe pool.
    # This keeps every generated prospect visually valid until more assets are added.
    if not matching:
        matching = all_safe_ids

    used = {str(face_id) for face_id in (used_portrait_ids or []) if face_id}
    assigned = assigned_this_class if assigned_this_class is not None else set()

    candidate_layers = [
        [face_id for face_id in matching if face_id not in used and face_id not in assigned],
        [face_id for face_id in matching if face_id not in assigned],
        [face_id for face_id in matching],
        [face_id for face_id in all_safe_ids if face_id not in used and face_id not in assigned],
        [face_id for face_id in all_safe_ids if face_id not in assigned],
        all_safe_ids,
    ]

    for candidates in candidate_layers:
        if candidates:
            face_id = rng.choice(candidates)
            assigned.add(face_id)
            return _rookie_face_entry(face_id)

    return _rookie_face_entry(all_safe_ids[0])


def _build_prospect(
    rank: int,
    season_year: int,
    class_type: str,
    rng: random.Random,
    used_names: set,
    portrait_info: Optional[Dict[str, str]] = None,
    identity_profile: Optional[Dict[str, Any]] = None,
    total_count: int = DRAFT_CLASS_FIXED_COUNT,
    overall_slot: Optional[int] = None,
) -> Dict[str, Any]:
    identity_profile = identity_profile or _make_identity_profile(rng)

    pos_weights = POSITION_WEIGHTS.get(class_type, POSITION_WEIGHTS["normal"])
    if class_type not in {"guard_heavy", "big_man_heavy"}:
        pos_weights = POSITION_WEIGHTS["normal"]

    pos = _weighted_choice(rng, pos_weights)
    secondary = rng.choice(SECONDARY_BY_POS.get(pos, [""]))
    archetype = rng.choice(ARCHETYPES_BY_POS[pos])

    age = _age_for_rank(rank, class_type, rng)
    tier, overall, potential = _talent_for_rank(rank, class_type, age, rng, total_count, overall_slot)

    height = rng.randint(*HEIGHT_RANGES[pos])
    weight = rng.randint(*WEIGHT_RANGES[pos])

    # Build archetype-shaped attributes, then calibrate them against the same
    # overall formula used by progression / Player Editor backend.
    # This prevents generated rookies from being displayed as 72-78 OVR while
    # secretly calculating as 80+ OVR once progression normalizes them.
    target_overall = int(_clamp(overall, MIN_GENERATED_DRAFT_OVR, MAX_GENERATED_DRAFT_OVR))
    attrs = _build_attrs(rng, pos, archetype, target_overall, potential, class_type)
    attrs = _calibrate_attrs_to_source_overall(attrs, pos, target_overall, rng)
    overall = _calc_overall_from_attrs(attrs, pos)
    potential = _clamp(max(overall, potential), overall, MAX_GENERATED_DRAFT_POTENTIAL)
    off_rating, def_rating, scoring_rating = _ratings_from_attrs(attrs)

    boom_bust = round(rng.uniform(0.12, 0.55), 2)
    if tier in {"Generational", "Franchise"}:
        boom_bust = round(rng.uniform(0.08, 0.40), 2)
    if tier == "Undrafted":
        boom_bust = round(rng.uniform(0.20, 0.66), 2)

    floor = _clamp(overall - rng.randint(2, 8), 45, overall)
    ceiling = _clamp(potential + rng.choice([0, 0, 1]), potential, MAX_GENERATED_DRAFT_POTENTIAL)

    visible_rank = _clamp(rank + rng.randint(-5, 7), 1, 110)
    if rank <= 5:
        visible_rank = _clamp(rank + rng.randint(-1, 2), 1, 10)

    name = _make_name(rng, used_names, identity_profile)
    birth_month = rng.randint(1, 12)
    birth_day = rng.randint(1, 28)

    prospect_id = f"draft_{season_year}_{rank:03d}_{name.lower().replace(' ', '_').replace('.', '')}"
    portrait_info = portrait_info or {}
    portrait_id = portrait_info.get("id") or ""
    portrait_url = portrait_info.get("url") or ""

    return {
        "id": prospect_id,
        "draftClassYear": season_year,
        "name": name,
        "portraitId": portrait_id,
        "portraitFamilyId": portrait_id,
        "portraitVariant": "rookie",
        "headshot": portrait_url,
        "image": portrait_url,
        "img": portrait_url,
        "age": age,
        "birthMonth": birth_month,
        "birthDay": birth_day,
        "pos": pos,
        "secondaryPos": secondary,
        "height": height,
        "weight": weight,
        # Keep the rookie source in multiple aliases so draft conversion/front-end
        # code can display it consistently after a player is drafted.
        "college": identity_profile.get("college") or rng.choice(COLLEGES),
        "school": identity_profile.get("college") or rng.choice(COLLEGES),
        "university": identity_profile.get("college") or "",
        "academy": identity_profile.get("college") or "",
        "academyName": identity_profile.get("college") or "",
        "sourceName": identity_profile.get("college") or "",
        "draftSource": identity_profile.get("college") or "",
        "sourceType": identity_profile.get("collegeBucket") or "college",
        "collegeBucket": identity_profile.get("collegeBucket") or "college",
        "nationality": identity_profile.get("nationality") or rng.choice(NATIONALITIES),
        "ethnicityGroup": identity_profile.get("ethnicityGroup") or "other",
        "subgroup": identity_profile.get("subgroup") or "other",
        "skinTone": identity_profile.get("skinTone") or "medium",
        "identityKey": identity_profile.get("identityKey") or "unknown",
        "region": identity_profile.get("region") or "unknown",
        "archetype": archetype,
        "tier": tier,
        "overall": overall,
        "potential": potential,
        "floor": floor,
        "ceiling": ceiling,
        "attrs": attrs,
        "offRating": off_rating,
        "defRating": def_rating,
        "stamina": _clamp(rng.randint(72, 91) + (2 if age >= 21 else 0), 60, 96),
        "scoringRating": scoring_rating,
        "draftProjection": visible_rank,
        "trueRank": rank,
        "scouting": {
            "projectedRangeLow": _clamp(visible_rank - rng.randint(1, 7), 1, 100),
            "projectedRangeHigh": _clamp(visible_rank + rng.randint(3, 12), 1, 100),
            "scoutedOverallRange": [
                _clamp(overall - rng.randint(1, 4), 45, 99),
                _clamp(overall + rng.randint(1, 4), 45, 99),
            ],
            "scoutedPotentialRange": [
                _clamp(potential - rng.randint(2, 7), overall, 99),
                _clamp(potential + rng.randint(1, 4), overall, MAX_GENERATED_DRAFT_POTENTIAL),
            ],
        },
        "traits": {
            "nbaReady": round(max(0.05, min(0.98, (overall - 55) / 32)), 2),
            "boomBust": boom_bust,
            "workEthic": round(rng.uniform(0.35, 0.95), 2),
            "injuryRisk": round(rng.uniform(0.04, 0.24), 2),
            "starUpside": round(max(0.02, min(0.98, (potential - 70) / 29)), 2),
        },
    }



def _repair_prospect_to_target_overall(prospect: Dict[str, Any], target_overall: int, rng: random.Random, class_type: str) -> Dict[str, Any]:
    target = int(_clamp(target_overall, MIN_GENERATED_DRAFT_OVR, MAX_GENERATED_DRAFT_OVR))
    pos = prospect.get("pos") or "SF"
    attrs = prospect.get("attrs") if isinstance(prospect.get("attrs"), list) else []
    if attrs:
        attrs = _calibrate_attrs_to_source_overall(attrs, pos, target, rng)
    else:
        attrs = _build_attrs(rng, pos, prospect.get("archetype") or "Two-Way Wing", target, _safe_int(prospect.get("potential"), target), class_type)
        attrs = _calibrate_attrs_to_source_overall(attrs, pos, target, rng)

    actual = _calc_overall_from_attrs(attrs, pos)
    prospect["attrs"] = attrs
    prospect["overall"] = actual
    prospect["potential"] = _clamp(max(actual, _safe_int(prospect.get("potential"), actual)), actual, MAX_GENERATED_DRAFT_POTENTIAL)
    prospect["offRating"], prospect["defRating"], prospect["scoringRating"] = _ratings_from_attrs(attrs)
    prospect["floor"] = _clamp(min(_safe_int(prospect.get("floor"), actual - 4), actual), 45, actual)
    prospect["ceiling"] = _clamp(max(_safe_int(prospect.get("ceiling"), prospect["potential"]), prospect["potential"]), prospect["potential"], MAX_GENERATED_DRAFT_POTENTIAL)
    prospect["tier"] = _tier_label_for_rank(_safe_int(prospect.get("trueRank"), 100), class_type, prospect["overall"], prospect["potential"])
    return prospect


def _repair_prospects_to_overall_slots(prospects: List[Dict[str, Any]], overall_slots: List[int], rng: random.Random, class_type: str) -> List[Dict[str, Any]]:
    repaired: List[Dict[str, Any]] = []
    for index, prospect in enumerate(prospects):
        target = overall_slots[index] if index < len(overall_slots) else MIN_GENERATED_DRAFT_OVR
        if _safe_int(prospect.get("overall"), 0) != target:
            prospect = _repair_prospect_to_target_overall(prospect, target, rng, class_type)
        repaired.append(prospect)
    return repaired

def generate_draft_class(
    season_year: int = 2026,
    count: int = DRAFT_CLASS_FIXED_COUNT,
    seed: Optional[int] = None,
    class_type: Optional[str] = None,
    used_portrait_ids: Optional[List[str]] = None,
    portrait_count: int = ROOKIE_FACE_COUNT,
) -> Dict[str, Any]:
    generated_fresh_seed = seed is None
    if seed is None:
        seed = _fresh_generation_seed("draft_class", season_year, DRAFT_CLASS_FIXED_COUNT, class_type or "auto")

    rng = random.Random(seed)
    if not class_type:
        class_type = rng.choices(
            CLASS_TYPES,
            weights = [0.12, 0.43, 0.15, 0.10, 0.06, 0.06, 0.04, 0.04],
        )[0]

    requested_count = _safe_int(count, DRAFT_CLASS_FIXED_COUNT)
    overall_slots, distribution_profile = _overall_slots_for_class(class_type, rng)
    count = DRAFT_CLASS_FIXED_COUNT
    used_names = set()
    assigned_portrait_ids = set()
    prospects = []

    for rank in range(1, count + 1):
        identity_profile = _make_identity_profile(rng)
        portrait_info = _choose_portrait_for_identity(
            identity_profile = identity_profile,
            rng = rng,
            used_portrait_ids = used_portrait_ids,
            assigned_this_class = assigned_portrait_ids,
            portrait_count = portrait_count,
        )
        prospects.append(
            _build_prospect(
                rank,
                season_year,
                class_type,
                rng,
                used_names,
                portrait_info,
                identity_profile,
                total_count = count,
                overall_slot = overall_slots[rank - 1],
            )
        )

    # Sort by true rank, but keep draftProjection as the visible noisy board.
    prospects = sorted(prospects, key = lambda p: p["trueRank"])
    prospects = _repair_prospects_to_overall_slots(prospects, overall_slots, rng, class_type)

    return {
        "ok": True,
        "version": AUTOGENERATED_DRAFT_CLASS_VERSION,
        "seasonYear": season_year,
        "seed": seed,
        "seedMode": "fresh_random" if generated_fresh_seed else "provided",
        "classType": class_type,
        "count": len(prospects),
        "draftClass": prospects,
        "classMeta": {
            "seasonYear": season_year,
            "classType": class_type,
            "seed": seed,
            "seedMode": "fresh_random" if generated_fresh_seed else "provided",
            "prospectCount": len(prospects),
            "requestedCount": requested_count,
            "forcedCount": DRAFT_CLASS_FIXED_COUNT,
            "distributionProfile": distribution_profile,
            "portraitCount": portrait_count,
            "activeSafePortraitCount": len(SAFE_ROOKIE_FACE_IDS),
            "activeSafePortraitIds": list(SAFE_ROOKIE_FACE_IDS),
            "assignedPortraitCount": len([p for p in prospects if p.get("portraitId")]),
            "identityProfiles": sorted(list({p.get("identityKey") for p in prospects if p.get("identityKey")})),
            "ratingModel": DRAFT_RATING_MODEL_VERSION,
            "minGeneratedOverall": MIN_GENERATED_DRAFT_OVR,
            "maxGeneratedOverall": MAX_GENERATED_DRAFT_OVR,
            "maxGeneratedPotential": MAX_GENERATED_DRAFT_POTENTIAL,
            "overallDistribution": _overall_distribution_summary(prospects),
            "targetOverallDistribution": distribution_profile.get("targetDistribution", {}),
            "summary": f"{season_year} generated {class_type.replace('_', ' ')} draft class",
        },
    }


def handle_request(request: Dict[str, Any]) -> Dict[str, Any]:
    req = _plain(request) or {}
    payload = req.get("payload") or {}
    action = req.get("action") or "generate_draft_class"

    if action not in {"generate_draft_class", "create_draft_class", "load_or_generate_draft_class"}:
        return {
            "ok": False,
            "reason": f"UNKNOWN_DRAFT_CLASS_ACTION: {action}",
            "version": AUTOGENERATED_DRAFT_CLASS_VERSION,
        }

    season_year = _safe_int(payload.get("seasonYear"), 2026)
    count = _safe_int(payload.get("count"), DRAFT_CLASS_FIXED_COUNT)
    seed = payload.get("seed")
    seed = _safe_int(seed, _stable_seed("draft_class", season_year)) if seed is not None else None
    class_type = payload.get("classType") or payload.get("classStrength") or None
    used_portrait_ids = payload.get("usedPortraitIds") or payload.get("usedPortraitIDs") or []
    portrait_count = _safe_int(payload.get("portraitCount"), ROOKIE_FACE_COUNT)

    return generate_draft_class(
        season_year = season_year,
        count = count,
        seed = seed,
        class_type = class_type,
        used_portrait_ids = used_portrait_ids,
        portrait_count = portrait_count,
    )
