def _normalize_attrs_for_overall(attrs: Any, default: int = 75) -> List[int]:
    out: List[int] = []
    if isinstance(attrs, list):
        for value in attrs[:15]:
            out.append(_clamp(_safe_int(value, default), 25, 99))

    while len(out) < 15:
        out.append(default)

    return out


def _calc_overall_from_attrs(attrs: List[int], pos: str) -> int:
    p = OVERALL_POS_PARAMS.get(str(pos or "SF").upper(), OVERALL_POS_PARAMS["SF"])
    safe_attrs = _normalize_attrs_for_overall(attrs)

    weighted = sum(float(w) * float(safe_attrs[i]) for i, w in enumerate(p["weights"]))
    primary_indices = [max(0, int(i) - 1) for i in p["prim"]]
    peak = max(float(safe_attrs[i]) for i in primary_indices)

    blended = float(p["alpha"]) * peak + (1.0 - float(p["alpha"])) * weighted
    overall = round(min(99, max(60, 60 + 39 * (1 / (1 + math.exp(-0.12 * (blended - 77)))))))

    num_90 = len([a for a in safe_attrs if a >= 90])
    if num_90 >= 3:
        overall = min(99, overall + (num_90 - 2))

    return int(overall)


def _overall_impact_order(pos: str, rng: random.Random, direction: int, attrs: List[int]) -> List[int]:
    p = OVERALL_POS_PARAMS.get(str(pos or "SF").upper(), OVERALL_POS_PARAMS["SF"])
    primary_indices = {max(0, int(i) - 1) for i in p["prim"]}
    scored: List[Tuple[float, int]] = []

    for idx, weight in enumerate(p["weights"]):
        impact = float(weight) + (0.08 if idx in primary_indices else 0.0)
        if direction < 0:
            impact += max(0.0, (attrs[idx] - 70) / 150.0)
        else:
            impact += max(0.0, (80 - attrs[idx]) / 180.0)
        impact += rng.random() * 0.01
        scored.append((impact, idx))

    scored.sort(reverse = True)
    return [idx for _, idx in scored]


def _calibrate_attrs_to_source_overall(
    attrs: List[int],
    pos: str,
    target_overall: int,
    rng: random.Random,
) -> List[int]:
    # The source formula cannot represent OVR below 60, so generated prospects
    # should not pretend to be 55-59 and then jump when progression recalculates.
    target = int(_clamp(target_overall, 60, 99))
    current_attrs = _normalize_attrs_for_overall(attrs)

    best_attrs = list(current_attrs)
    best_dist = abs(_calc_overall_from_attrs(best_attrs, pos) - target)

    for _ in range(900):
        current = _calc_overall_from_attrs(current_attrs, pos)
        current_dist = abs(current - target)

        if current_dist < best_dist:
            best_attrs = list(current_attrs)
            best_dist = current_dist

        if current == target:
            return current_attrs

        direction = 1 if current < target else -1
        candidates = [
            idx
            for idx in _overall_impact_order(pos, rng, direction, current_attrs)
            if (direction > 0 and current_attrs[idx] < 99) or (direction < 0 and current_attrs[idx] > 25)
        ]

        if not candidates:
            break

        chosen_attrs: Optional[List[int]] = None
        chosen_dist = 999
        chosen_overshoots = True

        for idx in candidates:
            trial = list(current_attrs)
            trial[idx] = int(_clamp(trial[idx] + direction, 25, 99))
            trial_overall = _calc_overall_from_attrs(trial, pos)
            trial_dist = abs(trial_overall - target)
            overshoots = (direction > 0 and trial_overall > target) or (direction < 0 and trial_overall < target)

            if trial_dist < chosen_dist or (
                trial_dist == chosen_dist and chosen_overshoots and not overshoots
            ):
                chosen_attrs = trial
                chosen_dist = trial_dist
                chosen_overshoots = overshoots

        if chosen_attrs is None:
            break

        # Allow flat steps because the sigmoid formula often needs several
        # one-point attribute moves before OVR changes by one.
        if chosen_dist > current_dist and current_dist <= best_dist and current_dist <= 1:
            break

        current_attrs = chosen_attrs

    return best_attrs



def _tier_for_rank(rank: int, class_type: str, rng: random.Random) -> Tuple[str, Tuple[int, int], Tuple[int, int]]:
    generational_cut = 1 if class_type == "generational" else 0
    if rank == 1 and (class_type == "generational" or rng.random() < 0.18):
        return "Generational", (80, 84), (94, 99)

    if rank <= 3 + (1 if class_type == "star_heavy" else 0):
        return "Franchise", (76, 81), (90, 97)

    if rank <= 8 + generational_cut:
        return "High Lottery", (72, 78), (85, 94)

    if rank <= (26 if class_type == "deep" else 22):
        return "First Round", (68, 75), (78, 90)

    if rank <= 45:
        return "Second Round", (62, 71), (72, 85)

    return "Undrafted", (55, 67), (65, 82)


def _adjust_for_class_type(overall: int, potential: int, rank: int, class_type: str) -> Tuple[int, int]:
    if class_type == "weak":
        if rank <= 10:
            potential -= 2
        overall -= 1
    elif class_type == "deep":
        if 12 <= rank <= 45:
            overall += 1
            potential += 1
    elif class_type == "star_heavy":
        if rank <= 6:
            potential += 2
    elif class_type == "defensive_class":
        potential += 0
    return _clamp(overall, 50, 88), _clamp(potential, overall, 99)


def _build_attrs(
    rng: random.Random,
    pos: str,
    archetype: str,
    overall: int,
    potential: int,
    class_type: str,
) -> List[int]:
    bias = ARCHETYPE_BIASES.get(archetype, [0] * 15)
    base = overall - 2

    attrs = []
    for idx, b in enumerate(bias):
        noise = rng.randint(-6, 6)
        value = base + b + noise
        attrs.append(_clamp(value, 25, 99))

    if pos == "PG":
        attrs[9] = min(attrs[9], overall - 5)
        attrs[10] = min(attrs[10], overall - 10)
        attrs[12] = min(attrs[12], overall - 5)
    elif pos == "C":
        attrs[4] = min(attrs[4], overall + 1)
        attrs[6] = min(attrs[6], overall)
        attrs[8] = min(attrs[8], overall + 2)

    if class_type == "defensive_class":
        for idx in [8, 9, 10, 11, 12, 14]:
            attrs[idx] = _clamp(attrs[idx] + rng.randint(2, 6), 25, 99)

    # Make true stars feel a bit cleaner across IQ and stamina-adjacent skills.
    if potential >= 92:
        attrs[13] = _clamp(attrs[13] + rng.randint(2, 5), 25, 99)
        attrs[14] = _clamp(attrs[14] + rng.randint(1, 4), 25, 99)

    return attrs


def _ratings_from_attrs(attrs: List[int]) -> Tuple[int, int, float]:
    off_indices = [0, 1, 2, 3, 4, 5, 7, 13]
    def_indices = [8, 9, 10, 11, 12, 14]

    off_rating = round(sum(attrs[i] for i in off_indices) / len(off_indices))
    def_rating = round(sum(attrs[i] for i in def_indices) / len(def_indices))

    scoring_rating = (
        attrs[0] * 0.28
        + attrs[1] * 0.21
        + attrs[2] * 0.24
        + attrs[4] * 0.10
        + attrs[7] * 0.08
        + attrs[13] * 0.09
    )

    return int(off_rating), int(def_rating), float(scoring_rating)


def _weighted_profile_choice(rng: random.Random) -> Dict[str, Any]:
    total = sum(float(profile.get("weight", 0.0)) for profile in IDENTITY_PROFILES)
    ticket = rng.random() * total
    running = 0.0

    for profile in IDENTITY_PROFILES:
        running += float(profile.get("weight", 0.0))
        if ticket <= running:
            return dict(profile)

    return dict(IDENTITY_PROFILES[0])


def _weighted_value_choice(rng: random.Random, weighted: List[Tuple[str, float]], fallback: str = "") -> str:
    if not weighted:
        return fallback

    total = sum(float(weight) for _, weight in weighted)
    ticket = rng.random() * total
    running = 0.0

    for item, weight in weighted:
        running += float(weight)
        if ticket <= running:
            return item

    return weighted[-1][0]


def _make_identity_profile(rng: random.Random) -> Dict[str, Any]:
    profile = _weighted_profile_choice(rng)
    profile_key = profile.get("key") or "unknown"
    nationality = _weighted_value_choice(rng, profile.get("nationalityWeights") or [], "USA")
    college_bucket = _weighted_value_choice(rng, profile.get("collegeWeights") or [], "college")
    college_options = COLLEGE_GROUPS.get(college_bucket, COLLEGES)

    first_names = _unique_text_list(
        list(profile.get("firstNames") or FIRST_NAMES)
        + list(ADDITIONAL_FIRST_NAMES_BY_PROFILE.get(profile_key) or [])
        + list(MEGA_FIRST_NAMES_BY_PROFILE.get(profile_key) or [])
    )
    last_names = _unique_text_list(
        list(profile.get("lastNames") or LAST_NAMES)
        + list(ADDITIONAL_LAST_NAMES_BY_PROFILE.get(profile_key) or [])
        + list(MEGA_LAST_NAMES_BY_PROFILE.get(profile_key) or [])
    )

    return {
        "identityKey": profile_key,
        "ethnicityGroup": profile.get("ethnicityGroup") or "other",
        "subgroup": profile.get("subgroup") or "other",
        "skinTone": profile.get("skinTone") or "medium",
        "region": profile.get("region") or "unknown",
        "nationality": nationality,
        "collegeBucket": college_bucket,
        "college": rng.choice(college_options),
        "firstNames": first_names or FIRST_NAMES,
        "lastNames": last_names or LAST_NAMES,
        "portraitRange": profile.get("portraitRange") or (1, ROOKIE_FACE_COUNT),
    }


def _make_name(rng: random.Random, used: set, identity_profile: Optional[Dict[str, Any]] = None) -> str:
    first_names = (identity_profile or {}).get("firstNames") or FIRST_NAMES
    last_names = (identity_profile or {}).get("lastNames") or LAST_NAMES

    # Try a lot harder than before so generated classes do not keep recycling
    # familiar combinations like Lamar Thomas or Bryce Bennett.
    for _ in range(500):
        name = f"{rng.choice(first_names)} {rng.choice(last_names)}"
        if name not in used and name not in OVERUSED_FULL_NAMES:
            used.add(name)
            return name

    # Extremely rare fallback if a pool somehow gets exhausted.
    for suffix in ["Jr.", "II", "III"]:
        name = f"{rng.choice(first_names)} {rng.choice(last_names)} {suffix}"
        if name not in used and name not in OVERUSED_FULL_NAMES:
            used.add(name)
            return name

    name = f"{rng.choice(first_names)} {rng.choice(last_names)}"
    used.add(name)
    return name


def _rookie_face_id(index: int) -> str:
    return f"rookie_face_{index:04d}"


def _rookie_face_url(face_id: str) -> str:
    return f"{ROOKIE_FACE_BASE_URL}/{face_id}.png"


def _rookie_face_entry(face_id: str) -> Dict[str, str]:
    url = _rookie_face_url(face_id)
    return {
        "id": face_id,
        "url": url,
        "headshot": url,
        "image": url,
        "img": url,
    }


def _portrait_ids_for_range(start: int, end: int, portrait_count: int) -> List[str]:
    start = max(1, _safe_int(start, 1))
    end = min(_safe_int(end, portrait_count), portrait_count)
    if end < start:
        return []
    return [_rookie_face_id(i) for i in range(start, end + 1)]


def _all_portrait_ids(portrait_count: int) -> List[str]:
    return [_rookie_face_id(i) for i in range(1, portrait_count + 1)]


def _choose_portrait_for_identity(
    identity_profile: Dict[str, Any],
    rng: random.Random,
    used_portrait_ids: Optional[List[str]] = None,
    assigned_this_class: Optional[set] = None,
    portrait_count: int = ROOKIE_FACE_COUNT,
) -> Dict[str, str]:
    # Phase 1 safety: only use manually verified portrait IDs.
    # Do not fall back to the old 1-100 range because most of those files do not exist
    # or have not been confirmed as clean transparent PNGs yet.
    profile_key = (identity_profile or {}).get("identityKey") or ""
    matching = list(SAFE_ROOKIE_FACE_IDS_BY_PROFILE.get(profile_key) or [])
    all_safe_ids = list(SAFE_ROOKIE_FACE_IDS)

    if not all_safe_ids:
        return _rookie_face_entry(_rookie_face_id(1))

    # If a profile has no verified bucket yet, use the full safe pool.
    # This keeps every generated prospect visually valid until more assets are added.
    if not matching:
        matching = all_safe_ids

    used = {str(face_id) for face_id in (used_portrait_ids or []) if face_id}
    assigned = assigned_this_class if assigned_this_class is not None else set()

    candidate_layers = [
        [face_id for face_id in matching if face_id not in used and face_id not in assigned],
        [face_id for face_id in matching if face_id not in assigned],
        [face_id for face_id in matching],
        [face_id for face_id in all_safe_ids if face_id not in used and face_id not in assigned],
        [face_id for face_id in all_safe_ids if face_id not in assigned],
        all_safe_ids,
    ]

    for candidates in candidate_layers:
        if candidates:
            face_id = rng.choice(candidates)
            assigned.add(face_id)
            return _rookie_face_entry(face_id)

    return _rookie_face_entry(all_safe_ids[0])


def _build_prospect(
    rank: int,
    season_year: int,
    class_type: str,
    rng: random.Random,
    used_names: set,
    portrait_info: Optional[Dict[str, str]] = None,
    identity_profile: Optional[Dict[str, Any]] = None,
) -> Dict[str, Any]:
    identity_profile = identity_profile or _make_identity_profile(rng)

    pos_weights = POSITION_WEIGHTS.get(class_type, POSITION_WEIGHTS["normal"])
    if class_type not in {"guard_heavy", "big_man_heavy"}:
        pos_weights = POSITION_WEIGHTS["normal"]

    pos = _weighted_choice(rng, pos_weights)
    secondary = rng.choice(SECONDARY_BY_POS.get(pos, [""]))
    archetype = rng.choice(ARCHETYPES_BY_POS[pos])

    tier, ovr_range, pot_range = _tier_for_rank(rank, class_type, rng)
    overall = rng.randint(*ovr_range)
    potential = rng.randint(max(overall, pot_range[0]), pot_range[1])
    overall, potential = _adjust_for_class_type(overall, potential, rank, class_type)

    age = rng.choices([18, 19, 20, 21, 22, 23], weights = [0.06, 0.44, 0.24, 0.16, 0.08, 0.02])[0]
    height = rng.randint(*HEIGHT_RANGES[pos])
    weight = rng.randint(*WEIGHT_RANGES[pos])

    # Build archetype-shaped attributes, then calibrate them against the same
    # overall formula used by progression / Player Editor backend.
    # This prevents generated rookies from being displayed as 72-78 OVR while
    # secretly calculating as 80+ OVR once progression normalizes them.
    target_overall = int(_clamp(overall, 60, 99))
    attrs = _build_attrs(rng, pos, archetype, target_overall, potential, class_type)
    attrs = _calibrate_attrs_to_source_overall(attrs, pos, target_overall, rng)
    overall = _calc_overall_from_attrs(attrs, pos)
    potential = max(overall, potential)
    off_rating, def_rating, scoring_rating = _ratings_from_attrs(attrs)

    boom_bust = round(rng.uniform(0.12, 0.55), 2)
    if tier in {"Generational", "Franchise"}:
        boom_bust = round(rng.uniform(0.08, 0.40), 2)
    if tier == "Undrafted":
        boom_bust = round(rng.uniform(0.20, 0.70), 2)

    floor = _clamp(overall - rng.randint(2, 8), 45, overall)
    ceiling = _clamp(potential + rng.randint(0, 3), potential, 99)

    visible_rank = _clamp(rank + rng.randint(-5, 7), 1, 110)
    if rank <= 5:
        visible_rank = _clamp(rank + rng.randint(-1, 2), 1, 10)

    name = _make_name(rng, used_names, identity_profile)
    birth_month = rng.randint(1, 12)
    birth_day = rng.randint(1, 28)

    prospect_id = f"draft_{season_year}_{rank:03d}_{name.lower().replace(' ', '_').replace('.', '')}"
    portrait_info = portrait_info or {}
    portrait_id = portrait_info.get("id") or ""
    portrait_url = portrait_info.get("url") or ""

    return {
        "id": prospect_id,
        "draftClassYear": season_year,
        "name": name,
        "portraitId": portrait_id,
        "portraitFamilyId": portrait_id,
        "portraitVariant": "rookie",
        "headshot": portrait_url,
        "image": portrait_url,
        "img": portrait_url,
        "age": age,
        "birthMonth": birth_month,
        "birthDay": birth_day,
        "pos": pos,
        "secondaryPos": secondary,
        "height": height,
        "weight": weight,
        # Keep the rookie source in multiple aliases so draft conversion/front-end
        # code can display it consistently after a player is drafted.
        "college": identity_profile.get("college") or rng.choice(COLLEGES),
        "school": identity_profile.get("college") or rng.choice(COLLEGES),
        "university": identity_profile.get("college") or "",
        "academy": identity_profile.get("college") or "",
        "academyName": identity_profile.get("college") or "",
        "sourceName": identity_profile.get("college") or "",
        "draftSource": identity_profile.get("college") or "",
        "sourceType": identity_profile.get("collegeBucket") or "college",
        "collegeBucket": identity_profile.get("collegeBucket") or "college",
        "nationality": identity_profile.get("nationality") or rng.choice(NATIONALITIES),
        "ethnicityGroup": identity_profile.get("ethnicityGroup") or "other",
        "subgroup": identity_profile.get("subgroup") or "other",
        "skinTone": identity_profile.get("skinTone") or "medium",
        "identityKey": identity_profile.get("identityKey") or "unknown",
        "region": identity_profile.get("region") or "unknown",
        "archetype": archetype,
        "tier": tier,
        "overall": overall,
        "potential": potential,
        "floor": floor,
        "ceiling": ceiling,
        "attrs": attrs,
        "offRating": off_rating,
        "defRating": def_rating,
        "stamina": _clamp(rng.randint(72, 91) + (2 if age >= 21 else 0), 60, 96),
        "scoringRating": scoring_rating,
        "draftProjection": visible_rank,
        "trueRank": rank,
        "scouting": {
            "projectedRangeLow": _clamp(visible_rank - rng.randint(1, 7), 1, 100),
            "projectedRangeHigh": _clamp(visible_rank + rng.randint(3, 12), 1, 100),
            "scoutedOverallRange": [
                _clamp(overall - rng.randint(1, 4), 45, 99),
                _clamp(overall + rng.randint(1, 4), 45, 99),
            ],
            "scoutedPotentialRange": [
                _clamp(potential - rng.randint(2, 7), overall, 99),
                _clamp(potential + rng.randint(1, 5), overall, 99),
            ],
        },
        "traits": {
            "nbaReady": round(max(0.05, min(0.98, (overall - 55) / 32)), 2),
            "boomBust": boom_bust,
            "workEthic": round(rng.uniform(0.35, 0.95), 2),
            "injuryRisk": round(rng.uniform(0.04, 0.24), 2),
            "starUpside": round(max(0.02, min(0.98, (potential - 70) / 29)), 2),
        },
    }


def generate_draft_class(
    season_year: int = 2026,
    count: int = 84,
    seed: Optional[int] = None,
    class_type: Optional[str] = None,
    used_portrait_ids: Optional[List[str]] = None,
    portrait_count: int = ROOKIE_FACE_COUNT,
) -> Dict[str, Any]:
    if seed is None:
        seed = _stable_seed("draft_class", season_year)

    rng = random.Random(seed)
    if not class_type:
        class_type = rng.choices(
            CLASS_TYPES,
            weights = [0.12, 0.43, 0.15, 0.10, 0.06, 0.06, 0.04, 0.04],
        )[0]

    count = max(60, min(110, _safe_int(count, 84)))
    used_names = set()
    assigned_portrait_ids = set()
    prospects = []

    for rank in range(1, count + 1):
        identity_profile = _make_identity_profile(rng)
        portrait_info = _choose_portrait_for_identity(
            identity_profile = identity_profile,
            rng = rng,
            used_portrait_ids = used_portrait_ids,
            assigned_this_class = assigned_portrait_ids,
            portrait_count = portrait_count,
        )
        prospects.append(
            _build_prospect(
                rank,
                season_year,
                class_type,
                rng,
                used_names,
                portrait_info,
                identity_profile,
            )
        )

    # Sort by true rank, but keep draftProjection as the visible noisy board.
    prospects = sorted(prospects, key = lambda p: p["trueRank"])

    return {
        "ok": True,
        "version": AUTOGENERATED_DRAFT_CLASS_VERSION,
        "seasonYear": season_year,
        "seed": seed,
        "classType": class_type,
        "count": len(prospects),
        "draftClass": prospects,
        "classMeta": {
            "seasonYear": season_year,
            "classType": class_type,
            "seed": seed,
            "prospectCount": len(prospects),
            "portraitCount": portrait_count,
            "activeSafePortraitCount": len(SAFE_ROOKIE_FACE_IDS),
            "activeSafePortraitIds": list(SAFE_ROOKIE_FACE_IDS),
            "assignedPortraitCount": len([p for p in prospects if p.get("portraitId")]),
            "identityProfiles": sorted(list({p.get("identityKey") for p in prospects if p.get("identityKey")})),
            "summary": f"{season_year} generated {class_type.replace('_', ' ')} draft class",
        },
    }


def handle_request(request: Dict[str, Any]) -> Dict[str, Any]:
    req = _plain(request) or {}
    payload = req.get("payload") or {}
    action = req.get("action") or "generate_draft_class"

    if action not in {"generate_draft_class", "create_draft_class", "load_or_generate_draft_class"}:
        return {
            "ok": False,
            "reason": f"UNKNOWN_DRAFT_CLASS_ACTION: {action}",
            "version": AUTOGENERATED_DRAFT_CLASS_VERSION,
        }

    season_year = _safe_int(payload.get("seasonYear"), 2026)
    count = _safe_int(payload.get("count"), 84)
    seed = payload.get("seed")
    seed = _safe_int(seed, _stable_seed("draft_class", season_year)) if seed is not None else None
    class_type = payload.get("classType") or payload.get("classStrength") or None
    used_portrait_ids = payload.get("usedPortraitIds") or payload.get("usedPortraitIDs") or []
    portrait_count = _safe_int(payload.get("portraitCount"), ROOKIE_FACE_COUNT)

    return generate_draft_class(
        season_year = season_year,
        count = count,
        seed = seed,
        class_type = class_type,
        used_portrait_ids = used_portrait_ids,
        portrait_count = portrait_count,
    )
