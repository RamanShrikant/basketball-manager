def _plain(value: Any) -> Any:
    if value is None:
        return None
    if isinstance(value, dict):
        return {str(k): _plain(v) for k, v in value.items()}
    if isinstance(value, list):
        return [_plain(v) for v in value]
    if isinstance(value, tuple):
        return [_plain(v) for v in value]
    try:
        if hasattr(value, "to_py"):
            return _plain(value.to_py())
    except Exception:
        pass
    return value


def _safe_int(value: Any, default: int = 0) -> int:
    try:
        if value is None:
            return default
        return int(float(value))
    except Exception:
        return default


def _stable_seed(*parts: Any) -> int:
    text = "|".join(str(part) for part in parts)
    total = 0
    for i, ch in enumerate(text):
        total = (total + (i + 1) * ord(ch)) % 2_147_483_647
    return total or 20260527


def _fresh_generation_seed(*parts: Any) -> int:
    """Create a new unpredictable seed when the caller does not provide one.

    The old generator used only season_year as the fallback seed, which made
    every auto-generated 2026 class identical across new leagues/tests. This
    keeps seeded calls reproducible, but makes unseeded auto classes fresh.
    """
    try:
        entropy = random.SystemRandom().randint(1, 2_147_483_646)
    except Exception:
        entropy = random.randint(1, 2_147_483_646)

    try:
        clock = time.time_ns()
    except Exception:
        clock = int(time.time() * 1_000_000)

    return _stable_seed("fresh_draft_class", *parts, clock, entropy)


def _clamp(value: float, lo: int, hi: int) -> int:
    return int(max(lo, min(hi, round(value))))


def _weighted_choice(rng: random.Random, weighted: List[Tuple[str, float]]) -> str:
    total = sum(weight for _, weight in weighted)
    ticket = rng.random() * total
    running = 0.0
    for item, weight in weighted:
        running += weight
        if ticket <= running:
            return item
    return weighted[-1][0]

